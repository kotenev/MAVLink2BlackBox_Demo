            public override void Write(byte[] src, int BYTE, int bytes)
            {
                time = ~0;
                if(time == 0)
                {
                    time = ~0;
                    b2p.state = Flow.STATE.STANDBY;
                    if(b2p.pack != null) b2p.pack = null;
                }
                for(; 0 < bytes--; BYTE++)
                {
                    switch(b2p.state)
                    {
                        case Flow.STATE.STANDBY:
                            b2p._fbbox = 0;
                            bits = 0;
                            b2p.state = Flow.STATE.PACK_ID;
                        goto case Flow.STATE.PACK_ID;
                        case Flow.STATE.PACK_ID:
                            b2p._fbbox = (b2p._fbbox << 8) | src[BYTE];
                            if(++bits < pack_id_bytes) continue;
                            b2p.mode = Flow.MODE.NONE;
                            Debug.Assert(b2p.pack == null);
                            if((b2p.pack = process(null, (int) b2p._fbbox)) == null)
                            {
                                failure("Unrecognized package ID = " + b2p._fbbox);
                                b2p.state = Flow.STATE.STANDBY;
                                continue;
                            }
                            b2p._fbbox = 0;
                            bits = 0;
                            b2p.field_bit = -1;
                            b2p._Ubbox = b2p._rbbox = b2p._cbbox = b2p._Sbbox = b2p.field_size = 0;
                            if(b2p.pack.meta.fields != null)
                            {
                                b2p.state = Flow.STATE.VARINT;
                                b2p.mode = Flow.MODE.OPTS_INFO;
                                continue;
                            }
                            break;
                        case Flow.STATE.BYTES:
                            b2p.pack.data[b2p._Sbbox++] = src[BYTE];
                            if(b2p._Sbbox < b2p._cbbox) continue;
                            break;
                        case Flow.STATE.VARINT:
                            b2p._fbbox |= (src[BYTE] & 0x7FUL) << bits;
                            bits += 7;
                            if((src[BYTE] & 0x80) != 0) continue;
                            bits = 0;
                            if(b2p.mode == Flow.MODE.OPTS_INFO)
                            {
                                b2p.pack.data = new byte[b2p.pack.meta.packMinBytes + (int) b2p._fbbox];
                                b2p._fbbox = 0;
                                b2p.mode = Flow.MODE.NONE;
                                break;
                            }
                            BitUtils.set_bytes(b2p._fbbox, b2p.field_size, b2p.pack.data, b2p._Sbbox);
                            b2p._fbbox = 0;
                            if((b2p._Sbbox += b2p.field_size) < b2p._cbbox) continue;
                            break;
                    }
                    if(b2p.next()) continue;
                    b2p.state = Flow.STATE.STANDBY;
                    b2p.mode = Flow.MODE.NONE;
                    if(b2p.pack != null)
                    {
                        process(b2p.pack, PROCESS_CHANNEL_REQEST);
                        b2p.pack = null;
                    }
                }
            }

            public override int Read(byte[] dst, int BYTE, int bytes)
            {
                int fix = BYTE;
                for(; 0 < bytes--; BYTE++)
                {
                    switch(p2b.state)
                    {
                        case Flow.STATE.STANDBY:
                        {
                            if((p2b.pack = process(null, PROCESS_CHANNEL_REQEST)) == null) return BYTE - fix;
                            p2b.state = Flow.STATE.PACK_ID;
                            p2b._fbbox = p2b.pack.meta.id;
                            p2b.field_bit = 8 * (pack_id_bytes - 1);
                        }
                        goto case Flow.STATE.PACK_ID;
                        case Flow.STATE.PACK_ID:
                            dst[BYTE] = (byte)(p2b._fbbox >> p2b.field_bit);
                            if(-1 < (p2b.field_bit -= 8)) continue;
                            p2b._fbbox = 0;
                            p2b.field_bit = -1;
                            p2b._Ubbox = p2b._rbbox = p2b._cbbox = p2b._Sbbox = p2b.field_size = 0;
                            p2b.mode = Flow.MODE.NONE;
                            if(p2b.pack.meta.fields != null)
                            {
                                p2b.state = Flow.STATE.VARINT;
                                Pack.Meta meta = p2b.pack.meta;
                                p2b.setPack(p2b.pack);
                                _hbbox(p2b);
                                p2b._fbbox = (ulong)(p2b._ybbox + 1 - meta.packMinBytes);
                                p2b._Ubbox = p2b._rbbox = p2b._cbbox = p2b._Sbbox = p2b.field_size = 0;
                                continue;
                            }
                            break;
                        case Flow.STATE.BYTES:
                            dst[BYTE] = p2b.pack.data[p2b._Sbbox++];
                            if(p2b._Sbbox < p2b._cbbox) continue;
                            break;
                        case Flow.STATE.VARINT:
                            if((p2b._fbbox & ~0x7FUL) != 0)
                            {
                                dst[BYTE] = (byte)(p2b._fbbox & 0x7F | 0x80);
                                p2b._fbbox >>= 7;
                                continue;
                            }
                            dst[BYTE] = (byte) p2b._fbbox;
                            if((p2b._Sbbox += p2b.field_size) < p2b._cbbox)
                            {
                                p2b._fbbox = BitUtils.get_bytes(p2b.pack.data, p2b._Sbbox, p2b.field_size);
                                continue;
                            }
                            break;
                    }
                    if(p2b.next())
                    {
                        if(p2b.state == Flow.STATE.VARINT) p2b._fbbox = BitUtils.get_bytes(p2b.pack.data, p2b._Sbbox, p2b.field_size);
                        continue;
                    }
                    p2b.state = Flow.STATE.STANDBY;
                    p2b.mode = Flow.MODE.NONE;
                    p2b.pack = null;
                }
                return BYTE - fix;
            }

            internal class Flow : Pack.Meta.Field.Bounds
            {
                internal ushort crc;

                internal MODE mode = MODE.NONE;

                internal STATE state = STATE.STANDBY;

                internal ulong _fbbox;

                internal virtual bool next()
                {
                    var meta = pack.meta;
                    var data = pack.data;
                    int delta;
                    if(field_bit == -1)
                        switch(field_size)
                        {
                            case 0:
                                if(0 < meta._2)
                                {
                                    _cbbox += (int) meta._2 * (field_size = 2);
                                    state = STATE.VARINT;
                                    return true;
                                }
                            goto case 2;
                            case 2:
                                if(0 < meta._4)
                                {
                                    _cbbox += (int) meta._4 * (field_size = 4);
                                    state = STATE.VARINT;
                                    return true;
                                }
                            goto case 4;
                            case 4:
                                if(0 < meta._8)
                                {
                                    _cbbox += (int) meta._8 * (field_size = 8);
                                    state = STATE.VARINT;
                                    return true;
                                }
                            goto case 8;
                            case 8:
                                if(_Sbbox < (_cbbox = (int) meta.packMinBytes))
                                {
                                    state = STATE.BYTES;
                                    field_size = 1;
                                    return true;
                                }
                                mode = MODE.NONE;
                                goto default;
                            default:
                                if(meta.fields == null)
                                    return false;
                                int fix = _Sbbox;
                                setPack(pack);
                                if(mode == MODE.NONE && fix < _cbbox)
                                {
                                    _Sbbox = fix;
                                    state = STATE.BYTES;
                                    mode = MODE.OPTS_INFO;
                                    field_size = 1;
                                    return true;
                                }
                                mode = MODE.NONE;
                                if((delta = BitUtils.first_1(data, (int) meta._Xbbox, (int)(_Ubbox - meta._Xbbox), false)) == -1) return false;
                                field_bit = (int) meta._Xbbox - 1;
                                break;
                        }
                    else
                    {
                        if(field_bit - meta._Xbbox == meta.fields.Length - 1 || (delta = BitUtils.first_1(data, field_bit + 1, _Ubbox - (field_bit + 1), false)) == -1) return false;
                    }
                    do
                    {
                        field_bit += delta + 1;
                        var fld = pack.meta.fields[field_bit - meta._Xbbox];
                        state = fld.varint ? STATE.VARINT : STATE.BYTES;
                        _rbbox = _Ubbox;
                        _Sbbox = _cbbox;
                        if(0 < fld.length)
                            if(0 < fld.size)
                                _cbbox += fld._ibbox * fld.length * (field_size = fld.size);
                            else
                                _Ubbox += fld._ibbox * fld.length * fld.size;
                        else setBounds(fld, this);
                        if(_Sbbox == _cbbox)
                            if((delta = BitUtils.first_1(data, field_bit + 1, _Ubbox - (field_bit + 1), false)) == -1)
                                return false;
                            else
                                continue;
                        break;
                    }
                    while(true);
                    return true;
                }

                internal enum STATE
                {
                    STANDBY,
                    PACK_ID,
                    VARINT,
                    VARINT_BR,
                    BYTES,
                    BYTES_BR
                }

                internal enum MODE
                {
                    PACKK_ID,
                    OPTS_INFO,
                    SET,
                    NONE,
                    CRC
                }
            }
        }



        static void init(Pack.Meta.Field fld, Pack.Meta.Field.Bounds.Inside ph)
        {
            switch(fld.type)
            {
                case 1:
                    ph.items = -fld.length;
                    ph.BIT = ph._Ubbox;
                    ph.BYTE = ph._Sbbox;
                    break;
                case 2:
                case 6:
                case 8:
                case 12:
                    ph.items = -fld.length;
                    ph.BYTE = ph._Sbbox;
                    ph.BIT = ph._rbbox - fld._Lbbox;
                    ph.field_item = Int32.MaxValue;
                    ph._nbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT, fld._ubbox);
                    ph._wbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT + fld._ubbox, fld._ubbox);
                    break;
                case 3:
                    ph.BYTE = ph._Sbbox;
                    ph.items = (int) BitUtils.get_bits(ph.pack.data, (ph.BIT = ph._rbbox - fld._Lbbox), -fld.length);
                    break;
                case 4:
                    ph.items = (int) BitUtils.get_bits(ph.pack.data, (ph.BIT = ph._rbbox - fld._Lbbox) + 2 * fld._ubbox, -fld.length);
                    ph.BYTE = ph._Sbbox;
                    ph.field_item = Int32.MaxValue;
                    ph._nbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT, fld._ubbox);
                    ph._wbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT + fld._ubbox, fld._ubbox);
                    break;
                case 5:
                case 11:
                    ph.items = 0;
                    ph.BYTE = ph._Sbbox;
                    ph.BIT = ph._rbbox - fld._Lbbox;
                    ph._wbbox = 0;
                    ph.field_item = Int32.MaxValue;
                    ph._nbbox = ph._gbbox;
                    break;
                case 7:
                    ph.items = -fld.length;
                    ph.BYTE = ph._Sbbox;
                    ph._wbbox = 0;
                    ph.BIT = ph._Ubbox;
                    ph.field_item = (ph._nbbox = ph._gbbox) == 0 ? Int32.MaxValue : 0;
                    break;
                case 9:
                    ph.items = (int) BitUtils.get_bits(ph.pack.data, (ph.BIT = ph._rbbox - fld._Lbbox), -fld.length);
                    ph.BYTE = ph._Sbbox;
                    ph._wbbox = 0;
                    ph.BIT = ph._Ubbox;
                    ph.field_item = (ph._nbbox = ph._gbbox) == 0 ? Int32.MaxValue : 0;
                    break;
                case 10:
                    ph.items = (int) BitUtils.get_bits(ph.pack.data, (ph.BIT = ph._rbbox - fld._Lbbox) + 2 * fld._ubbox, -fld.length);
                    ph.BYTE = ph._Sbbox;
                    ph.field_item = Int32.MaxValue;
                    ph._nbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT, fld._ubbox);
                    ph._wbbox = (int) BitUtils.get_bits(ph.pack.data, ph.BIT + fld._ubbox, fld._ubbox);
                    break;
                default:
                    Debug.Assert(false);
                    return;
            }
        }

        internal static void setBounds(Pack.Meta.Field fld, Pack.Meta.Field.Bounds ph)
        {
            ph.field_size = fld.size;
            switch(fld.type)
            {
                case 1:
                    int count = 1;
                    if(0 < fld._Fbbox)
                        for(int i = 0, _bit = ph._Ubbox; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                count *= (int) BitUtils.get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                    ph._Ubbox -= fld._Lbbox;
                    ph._cbbox += fld._ibbox * count * -fld.length * fld.size;
                    break;
                case 2:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    ph.field_size = fld.size;
                    int bit = ph._Ubbox;
                    ph._Ubbox -= count;
                    byte[] data = ph.pack.data;
                    while(ph._Ubbox < bit--)
                        if((data[bit >> 3] & 1 << (bit & 7)) == 0)
                            count--;
                    ph._cbbox += -fld.length * count * fld.size;
                    break;
                case 3:
                    ph.field_size = fld.size;
                    bit = ph._Ubbox;
                    ph._Ubbox -= fld._Lbbox;
                    int arrays_length = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox, -fld.length);
                    if(0 < fld._Fbbox)
                        for(int i = 0; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                arrays_length *= (int) BitUtils.get_bits(ph.pack.data, bit -= -fld.dims[i], -fld.dims[i]);
                    ph._cbbox += fld._ibbox * arrays_length * fld.size;
                    break;
                case 4:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    ph.field_size = fld.size;
                    bit = ph._Ubbox;
                    ph._Ubbox -= count;
                    data = ph.pack.data;
                    arrays_length = (int) BitUtils.get_bits(data, bit + 2 * fld._ubbox, -fld.length);
                    while(ph._Ubbox < bit--)
                        if((data[bit >> 3] & 1 << (bit & 7)) == 0)
                            count--;
                    ph._cbbox += count * arrays_length * fld.size;
                    break;
                case 5:
                    ph.field_size = fld.size;
                    count = fld._ibbox;
                    if(0 < fld._Fbbox)
                        for(int i = 0, _bit = ph._Ubbox; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                count *= (int) BitUtils.get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                    ph._Ubbox -= fld._Lbbox;
                    int all_arrays_sum = 0;
                    while(0 < count--) all_arrays_sum += (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= -fld.length, -fld.length);
                    ph._cbbox += all_arrays_sum * fld.size;
                    break;
                case 6:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    ph.field_size = fld.size;
                    bit = ph._Ubbox;
                    all_arrays_sum = 0;
                    data = ph.pack.data;
                    while(0 < count--)
                        if((data[--bit >> 3] & 1 << (bit & 7)) != 0)
                            all_arrays_sum += (int) BitUtils.get_bits(ph.pack.data, bit -= -fld.length, -fld.length);
                    ph._Ubbox = bit;
                    ph._cbbox += all_arrays_sum * fld.size;
                    break;
                case 7:
                    ph.field_size = 0;
                    count = 1;
                    if(0 < fld._Fbbox)
                        for(int i = 0, _bit = ph._Ubbox; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                count *= (int) BitUtils.get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                    ph._Ubbox -= fld._Lbbox + count * fld._ibbox * -fld.length * fld.size;
                    break;
                case 8:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    bit = ph._Ubbox;
                    ph.field_size = fld.size;
                    data = ph.pack.data;
                    for(int bits = -fld.length * fld.size; 0 < count--;)
                        if((data[--bit >> 3] & 1 << (bit & 7)) != 0)
                            bit -= bits;
                    ph._Ubbox = bit;
                    break;
                case 9:
                    ph.field_size = fld.size;
                    bit = ph._Ubbox;
                    ph._Ubbox -= fld._Lbbox;
                    arrays_length = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox, -fld.length);
                    if(0 < fld._Fbbox)
                        for(int i = 0; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                arrays_length *= (int) BitUtils.get_bits(ph.pack.data, bit -= -fld.dims[i], -fld.dims[i]);
                    ph._Ubbox -= fld._ibbox * arrays_length * fld.size;
                    break;
                case 10:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    bit = ph._Ubbox;
                    ph.field_size = fld.size;
                    data = ph.pack.data;
                    for(int bits = (int) BitUtils.get_bits(data, bit + 2 * fld._ubbox, -fld.length) * fld.size; 0 < count--;)
                        if((data[--bit >> 3] & 1 << (bit & 7)) != 0)
                            bit -= bits;
                    ph._Ubbox = bit;
                    break;
                case 11:
                    ph.field_size = fld.size;
                    count = fld._ibbox;
                    if(0 < fld._Fbbox)
                        for(int i = 0, _bit = ph._Ubbox; i < fld.dims.Length; i++)
                            if(fld.dims[i] < 0)
                                count *= (int) BitUtils.get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                    ph._Ubbox -= fld._Lbbox;
                    while(0 < count--)
                    {
                        int bits = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= -fld.length, -fld.length) * fld.size;
                        ph._Ubbox -= bits;
                    }
                    break;
                case 12:
                    count = (int) BitUtils.get_bits(ph.pack.data, ph._Ubbox -= fld._Lbbox, fld._ubbox);
                    if(count == 0) return;
                    ph.field_size = fld.size;
                    bit = ph._Ubbox;
                    all_arrays_sum = 0;
                    data = ph.pack.data;
                    while(0 < count--)
                        if((data[--bit >> 3] & 1 << (bit & 7)) != 0)
                        {
                            int len = (int) BitUtils.get_bits(ph.pack.data, bit -= -fld.length, -fld.length);
                            bit -= len * fld.size;
                        }
                    ph._Ubbox = bit;
                    break;
                default:
                    Debug.Assert(false);
                    return;
            }
        }

        static void insert(Pack.Meta.Field.Bounds.Inside ph, int fbit, int bits, int bytes)
        {
            if(ph.field_bit != fbit)
            {
                ph.BIT = ph._rbbox = ph._Ubbox;
                ph.BYTE = ph._Sbbox = ph._cbbox;
            }
            Pack.Meta meta = ph.pack.meta;
            if(ph._ybbox < 0) _hbbox(ph);
            int add_to_bits_bytes;
            if(ph.BIT == ph._Nbbox && ph.BYTE == ph._ybbox)
            {
                add_to_bits_bytes = meta._bbbox == 0 ? 0 : BitUtils.bits2bytes(fbit + bits - ph._Nbbox + 1);
                if(0 < add_to_bits_bytes || 0 < bytes)
                {
                    int old_pack_data_bytes = ph._ybbox;
                    Debug.Assert(old_pack_data_bytes == ph.pack.data.Length);
                    byte[] data = new byte[old_pack_data_bytes + add_to_bits_bytes + bytes];
                    if(ph.field_bit == -1 || bits == 0)
                        Array.Copy(ph.pack.data, 0, data, 0, old_pack_data_bytes);
                    else
                    {
                        BitUtils.copy_bits(ph.pack.data, 0,
                                           ph.field_bit + 1,
                                           data, 0);
                        BitUtils.copy_bits(ph.pack.data, ph.BIT,
                                           (old_pack_data_bytes << 3) - ph.BIT, data, ph.BIT + (add_to_bits_bytes << 3));
                    }
                    ph._ybbox += add_to_bits_bytes + bytes;
                    ph.pack.data = data;
                }
            }
            else
            {
                int add_bits_bits = meta._bbbox == 0 ? 0 : bits - (ph._Nbbox - (ph._kbbox + 1));
                add_to_bits_bytes = meta._bbbox == 0 ? 0 : BitUtils.bits2bytes(add_bits_bits);
                if(0 < add_to_bits_bytes || 0 < bytes)
                {
                    int old_pack_data_bytes = ph._ybbox;
                    Debug.Assert(old_pack_data_bytes == ph.pack.data.Length);
                    byte[] data = new byte[old_pack_data_bytes + add_to_bits_bytes + bytes];
                    if(bits == 0)
                        Array.Copy(ph.pack.data, 0, data, 0, ph.BYTE + 1);
                    else
                    {
                        BitUtils.copy_bits(ph.pack.data, 0,
                                           ph._kbbox + 1,
                                           data, 0);
                        BitUtils.copy_bits(ph.pack.data, ph._Nbbox,
                                           ph.BIT - ph._Nbbox, data, ph._Nbbox + (add_to_bits_bytes << 3) - bits);
                        BitUtils.copy_bits(ph.pack.data, ph.BIT, (ph.BYTE << 3) - ph.BIT, data, ph.BIT + (add_to_bits_bytes << 3));
                    }
                    Array.Copy(
                        ph.pack.data, ph.BYTE, data, ph.BYTE + add_to_bits_bytes + bytes, ph._ybbox - ph.BYTE);
                    ph._ybbox += add_to_bits_bytes + bytes;
                    ph.pack.data = data;
                }
                else
                {
                    BitUtils.copy_bits(ph.pack.data, ph._Nbbox,
                                       ph.BIT - ph._Nbbox, ph.pack.data, ph._Nbbox - bits);
                    BitUtils.set_0(ph.pack.data, ph.BIT - bits, bits);
                }
            }
            if(0 < meta._bbbox && 0 < add_to_bits_bytes)
            {
                int old_value = (int) BitUtils.get_bits(ph.pack.data, meta._Xbbox - meta._bbbox, meta._bbbox);
                BitUtils.set_bits((uint)(old_value + add_to_bits_bytes), meta._bbbox, ph.pack.data, meta._Xbbox - meta._bbbox);
            }
            int zazor_delta = (add_to_bits_bytes << 3) - bits;
            ph._Nbbox += zazor_delta;
            ph.BIT += add_to_bits_bytes << 3;
            ph.BYTE += add_to_bits_bytes;
            if(fbit == ph.field_bit)
            {
                ph._Ubbox += zazor_delta;
                ph._rbbox += add_to_bits_bytes << 3;
                ph._Sbbox += add_to_bits_bytes;
                ph._cbbox += add_to_bits_bytes + bytes;
            }
            else
            {
                ph._rbbox = ph._Ubbox = ph.BIT;
                ph._cbbox = ph._Sbbox = ph.BYTE;
                ph.pack.data[fbit >> 3] |= (byte)(1 << (fbit & 7));
                if(ph._kbbox < fbit) ph._kbbox = fbit;
                ph.field_bit = fbit;
                ph.field = meta.fields[ph.field_bit - meta._Xbbox];
            }
        }

        static bool visit_field(Pack.Meta.Field.Bounds.Inside ph, int fbit)
        {
            if(ph.field_bit == fbit) return true;
            Pack.Meta meta = ph.pack.meta;
            byte[] data = ph.pack.data;
            int delta;
            for(;;)
                if(ph.field_bit == -1)
                {
                    if((delta = BitUtils.first_1(data, meta._Xbbox, ph._Ubbox - meta._Xbbox, false)) == -1 ||
                            fbit < meta._Xbbox + delta) return false;
                    ph.field_bit = meta._Xbbox - 1;
                    break;
                }
                else if(fbit < ph.field_bit)
                    ph.setPack(ph.pack);
                else
                {
                    if(ph.field_bit - meta._Xbbox == meta.fields.Length - 1 ||
                            (delta = BitUtils.first_1(data, ph.field_bit + 1, ph._Ubbox - (ph.field_bit + 1), false)) == -1 ||
                            fbit < ph.field_bit + 1 + delta) return false;
                    break;
                }
            for(;;)
            {
                Pack.Meta.Field fld = meta.fields[(ph.field_bit += 1 + delta) - meta._Xbbox];
                ph.field = fld;
                ph._rbbox = ph._Ubbox;
                ph._Sbbox = ph._cbbox;
                if(0 < fld.length)
                    if(0 < fld.size)
                        ph._cbbox += fld._ibbox * fld.length * fld.size;
                    else
                        ph._Ubbox += fld._ibbox * fld.length * fld.size;
                else setBounds(fld, ph);
                if(ph.field_bit == fbit || ph.field_bit - meta._Xbbox == meta.fields.Length - 1 ||
                        (delta = BitUtils.first_1(data, ph.field_bit + 1, ph._Ubbox - (ph.field_bit + 1), false)) == -1 ||
                        fbit < ph.field_bit + 1 + delta)
                {
                    ph.items = fld.length;
                    ph.field_size = fld.size;
                    ph.BIT = ph._Ubbox;
                    ph.BYTE = ph._Sbbox;
                    if(fld.length < 1)
                    {
                        ph._gbbox = fld._ibbox;
                        if(0 < fld._Fbbox)
                        {
                            if(ph.D == null || ph.D.Length < fld.dims.Length) ph.D = new int[fld.dims.Length];
                            else
                                for(int i = ph.D.Length - 1; -1 < i;)
                                    ph.D[i--] = 0;
                            for(int i = 0, bit = ph._rbbox, dim; i < fld.dims.Length; i++)
                                if((dim = fld.dims[i]) < 1)
                                    ph._gbbox *= ph.D[i] = (int) BitUtils.get_bits(data, bit -= -dim, -dim);
                                else
                                    ph.D[i] = dim;
                        }
                        init(fld, ph);
                    }
                    return ph.field_bit == fbit;
                }
            }
        }

        static void _hbbox(Pack.Meta.Field.Bounds ph)
        {
            Pack.Meta meta = ph.pack.meta;
            ph._kbbox = ph.field_bit == -1 ? meta._Xbbox - 1 : ph.field_bit;
            int delta = ph.field_bit == -1 ? BitUtils.first_1(ph.pack.data, meta._Xbbox, ph._Ubbox - meta._Xbbox, false) : BitUtils.first_1(ph.pack.data, ph.field_bit + 1, ph._Ubbox - (ph.field_bit + 1), false);
            if(delta == -1)
            {
                ph._ybbox = ph._cbbox;
                ph._kbbox = ph.field_bit;
                ph._Nbbox = ph._Ubbox;
                Debug.Assert(ph._ybbox == ph.pack.data.Length);
            }
            else
            {
                int _Ubbox_FX = ph._Ubbox, _rbbox_FX = ph._rbbox, _Sbbox_FX = ph._Sbbox, _cbbox_FX = ph._cbbox, item_size_FX = ph.field_size;
                for(int max = meta._Xbbox + meta.fields.Length - 1;;)
                {
                    ph._kbbox += delta + 1;
                    ph._rbbox = ph._Ubbox;
                    ph._Sbbox = ph._cbbox;
                    Pack.Meta.Field fld = meta.fields[ph._kbbox - meta._Xbbox];
                    if(0 < fld.length)
                        if(0 < fld.size)
                            ph._cbbox += fld._ibbox * fld.length * fld.size;
                        else
                            ph._Ubbox += fld._ibbox * fld.length * fld.size;
                    else setBounds(fld, ph);
                    if(ph._kbbox == max || (delta = BitUtils.first_1(ph.pack.data, ph._kbbox + 1, ph._Ubbox - (ph._kbbox + 1), false)) == -1) break;
                }
                ph._ybbox = ph._cbbox;
                ph._Nbbox = ph._Ubbox;
                Debug.Assert(ph._ybbox == ph.pack.data.Length);
                ph._Ubbox = _Ubbox_FX;
                ph._rbbox = _rbbox_FX;
                ph._Sbbox = _Sbbox_FX;
                ph._cbbox = _cbbox_FX;
                ph.field_size = item_size_FX;
            }
        }

        public static bool insert_field(Pack.Meta.Field.Bounds.Inside ph, int fbit, int items, params int[] var_dims)
        {
            if(visit_field(ph, fbit)) return false;
            Pack.Meta.Field fld = ph.pack.meta.fields[fbit - ph.pack.meta._Xbbox];
            if(fld.type == 0)
            {
                if(0 < fld.size)
                {
                    insert(ph, fbit, 0, fld._ibbox * fld.length * fld.size);
                    ph._cbbox = (ph.BYTE = ph._Sbbox) + fld._ibbox * (ph.items = fld.length) * (ph.field_size = fld.size);
                }
                else
                {
                    insert(ph, fbit, fld._ibbox * fld.length * -fld.size, 0);
                    ph.BIT = ph._Ubbox = ph._rbbox + fld._ibbox * (ph.items = fld.length) * fld.size;
                }
                return true;
            }
            int total = fld._ibbox;
            if(var_dims != null)
                for(int i = var_dims.Length - 1; -1 < i; i--)
                    total *= var_dims[i];
            switch(fld.type)
            {
                case 1:
                    insert(ph, fbit, fld._Lbbox, total * -fld.length * fld.size);
                    break;
                case 2:
                case 6:
                case 8:
                case 12:
                    insert(ph, fbit, fld._Lbbox, 0);
                    break;
                case 3:
                    insert(ph, fbit, fld._Lbbox, total * items * fld.size);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph._rbbox - fld._Lbbox);
                    break;
                case 4:
                    insert(ph, fbit, fld._Lbbox, 0);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph._rbbox - fld._Lbbox + 2 * fld._ubbox);
                    break;
                case 5:
                case 11:
                    insert(ph, fbit, fld._Lbbox + total * -fld.length, 0);
                    break;
                case 7:
                    insert(ph, fbit, fld._Lbbox + total * -fld.length * fld.size, 0);
                    break;
                case 9:
                    insert(ph, fbit, fld._Lbbox + total * items * fld.size, 0);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph._rbbox - fld._Lbbox);
                    break;
                case 10:
                    insert(ph, fbit, fld._Lbbox, 0);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph._rbbox - fld._Lbbox + 2 * fld._ubbox);
                    break;
                default:
                    Debug.Assert(false);
                    return false;
            }
            ph._gbbox = total;
            if(0 < fld._Fbbox)
            {
                if(ph.D == null || ph.D.Length < fld.dims.Length) ph.D = new int[fld.dims.Length];
                else
                    for(int i = ph.D.Length - 1; -1 < i;)
                        ph.D[i--] = 0;
                for(int i = 0, k = 0, bit = ph._rbbox; i < fld.dims.Length; i++)
                    if(fld.dims[i] < 1)
                    {
                        ph.D[i] = var_dims[k];
                        BitUtils.set_bits((uint) var_dims[k], -fld.dims[i], ph.pack.data, bit -= -fld.dims[i]);
                        k++;
                    }
                    else
                        ph.D[i] = fld.dims[i];
            }
            setBounds(fld, ph);
            init(fld, ph);
            return true;
        }

        public static bool try_visit_item(Pack.Meta.Field.Bounds.Inside ph, int item)
        {
            Pack.Meta.Field fld = ph.field;
            switch(fld.type)
            {
                default:
                    Debug.Assert(false);
                    return false;
                case 2:
                case 4:
                    if(ph.field_item == item) return true;
                    if(ph._nbbox == 0 || item < ph._wbbox || ph._wbbox + ph._nbbox <= item) return false;
                    int bit = ph._rbbox - fld._Lbbox - 1 - (item - ph._wbbox);
                    if((ph.pack.data[bit >> 3] & 1 << (bit & 7)) == 0) return false;
                    if(item < ph.field_item)
                    {
                        ph.field_item = ph._wbbox;
                        ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        ph.BYTE = ph._Sbbox;
                    }
                    else
                    {
                        ph.BIT--;
                        ph.field_item++;
                        ph.BYTE += ph.items * fld.size;
                    }
                    for(int bytes = ph.items * fld.size;; ph.field_item++, ph.BIT--)
                        if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0)
                            if(ph.field_item == item)
                                break;
                            else
                                ph.BYTE += bytes;
                    return true;
                case 5:
                    if(ph._gbbox - 1 < item || BitUtils.get_bits(ph.pack.data, ph._rbbox - fld._Lbbox - (item + 1) * -fld.length, -fld.length) == 0) return false;
                    if(item < ph.field_item)
                    {
                        ph.field_item = 0;
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length);
                        ph.BYTE = ph._Sbbox;
                    }
                    for(; ph.field_item < item; ph.BYTE += ph.items * fld.size, ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length), ph.field_item++) ;
                    return true;
                case 6:
                    if(ph.field_item == item) return true;
                    if(item < ph._wbbox || ph._wbbox + ph._nbbox <= item) return false;
                    int field_item = ph.field_item;
                    bit = ph.BIT;
                    int BYTE = ph.BYTE;
                    if(item < field_item)
                    {
                        field_item = ph._wbbox;
                        bit = ph._rbbox - fld._Lbbox - 1;
                        BYTE = ph._Sbbox;
                    }
                    else
                    {
                        bit--;
                        field_item++;
                        BYTE += ph.items * fld.size;
                    }
                    for(int len_bits = -fld.length;; bit--, field_item++)
                        if((ph.pack.data[bit >> 3] & 1 << (bit & 7)) != 0)
                        {
                            bit -= len_bits;
                            if(field_item == item)
                            {
                                ph.field_item = field_item;
                                ph.BYTE = BYTE;
                                ph.BIT = bit;
                                ph.items = (int) BitUtils.get_bits(ph.pack.data, bit, len_bits);
                                return true;
                            }
                            BYTE += (int) BitUtils.get_bits(ph.pack.data, bit, len_bits) * fld.size;
                        }
                        else if(field_item == item)
                            return false;
                case 8:
                case 10:
                    if(ph.field_item == item) return true;
                    if(item < ph._wbbox || ph._wbbox + ph._nbbox <= item) return false;
                    field_item = ph.field_item;
                    bit = ph.BIT;
                    if(item < field_item)
                    {
                        field_item = ph._wbbox;
                        bit = ph._rbbox - fld._Lbbox - 1;
                    }
                    else
                    {
                        bit--;
                        field_item++;
                    }
                    for(int bits = ph.items * fld.size;; bit--, field_item++)
                        if((ph.pack.data[bit >> 3] & 1 << (bit & 7)) != 0)
                        {
                            bit -= bits;
                            if(field_item == item)
                            {
                                ph.field_item = item;
                                ph.BIT = bit;
                                return true;
                            }
                        }
                        else if(field_item == item)
                            return false;
                case 11:
                    if(ph._gbbox - 1 < item) return false;
                    field_item = ph.field_item;
                    bit = ph.BIT;
                    if(item < field_item)
                    {
                        field_item = -1;
                        bit = ph._rbbox - fld._Lbbox;
                    }
                    else if(item == ph.field_item)
                        return true;
                    int items;
                    do
                    {
                        int bits = fld.size * (items = (int) BitUtils.get_bits(ph.pack.data, bit -= -fld.length, -fld.length));
                        bit -= bits;
                        field_item++;
                    }
                    while(field_item < item);
                    if(items == 0) return false;
                    ph.BIT = bit;
                    ph.items = items;
                    ph.field_item = field_item;
                    return true;
                case 12:
                    if(ph.field_item == item) return true;
                    if(item < ph._wbbox || ph._wbbox + ph._nbbox <= item) return false;
                    field_item = ph.field_item;
                    bit = ph.BIT;
                    if(item < field_item)
                    {
                        field_item = ph._wbbox;
                        bit = ph._rbbox - fld._Lbbox - 1;
                    }
                    else
                    {
                        bit--;
                        field_item++;
                    }
                    for(int len_bits = -fld.length;; bit--, field_item++)
                        if((ph.pack.data[bit >> 3] & 1 << (bit & 7)) != 0)
                        {
                            bit -= len_bits;
                            if(field_item == item)
                            {
                                ph.field_item = field_item;
                                ph.items = (int) BitUtils.get_bits(ph.pack.data, bit, len_bits);
                                ph.BIT = bit - ph.items * fld.size;
                                return true;
                            }
                            bit -= (int) BitUtils.get_bits(ph.pack.data, bit, len_bits) * fld.size;
                        }
                        else if(field_item == item)
                            return false;
            }
        }


        public static void insert_item(Pack.Meta.Field.Bounds.Inside ph, int item, int items)
        {
            Pack.Meta.Field fld = ph.field;
            switch(fld.type)
            {
                default:
                    Debug.Assert(false);
                    return;
                case 2:
                case 4:
                    if(item < ph._wbbox || ph._nbbox == 0)
                    {
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        ph.BYTE = ph._Sbbox;
                        int ins_items = ph._nbbox == 0 ? 1 : ph._wbbox - item;
                        insert(ph, ph.field_bit, ins_items, ph.items * fld.size);
                        ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        BitUtils.set_bits((uint)(ph._wbbox = item), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox + fld._ubbox);
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    else if(item < ph._wbbox + ph._nbbox)
                    {
                        if(item < ph.field_item)
                        {
                            ph.field_item = ph._wbbox;
                            ph.BIT = ph._rbbox - fld._Lbbox - 1;
                            ph.BYTE = ph._Sbbox;
                        }
                        else
                        {
                            ph.BIT--;
                            ph.field_item++;
                            ph.BYTE += ph.items * fld.size;
                        }
                        for(int bytes = ph.items * fld.size; ph.field_item < item; ph.field_item++, ph.BIT--)
                            if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0)
                                ph.BYTE += bytes;
                        Debug.Assert((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) == 0);
                        insert(ph, ph.field_bit, 0, ph.items * fld.size);
                    }
                    else
                    {
                        int ins_items = item + 1 - (ph._wbbox + ph._nbbox);
                        ph.BIT = ph._Ubbox;
                        ph.BYTE = ph._cbbox;
                        insert(ph, ph.field_bit, ins_items, ph.items * fld.size);
                        ph.BIT = ph._Ubbox;
                        ph.BYTE = ph._cbbox - ph.items * fld.size;
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    ph.pack.data[ph.BIT >> 3] |= (byte)(1 << (ph.BIT & 7));
                    break;
                case 5:
                    if(ph._gbbox - 1 < item) throw new Exception("No room for item=" + item + ". The _gbbox =" + ph._gbbox);
                    if(item < ph.field_item)
                    {
                        ph.field_item = 0;
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        ph.BYTE = ph._Sbbox;
                        ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length);
                    }
                    for(; ph.field_item < item; ph.BYTE += ph.items * fld.size, ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length), ph.field_item++) ;
                    if(ph.items != 0) throw new Exception("Already allocated");
                    insert(ph, ph.field_bit, 0, items * fld.size);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph.BIT);
                    ph.items = items;
                    break;
                case 6:
                    int len_bits = -fld.length;
                    if(item < ph._wbbox || ph._nbbox == 0)
                    {
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        ph.BYTE = ph._Sbbox;
                        int ins_items = ph._nbbox == 0 ? 1 : ph._wbbox - item;
                        insert(ph, ph.field_bit, ins_items + len_bits, items * fld.size);
                        ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        BitUtils.set_bits((uint)(ph._wbbox = item), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox + fld._ubbox);
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    else if(item < ph._wbbox + ph._nbbox)
                    {
                        if(item < ph.field_item)
                        {
                            ph.field_item = ph._wbbox;
                            ph.BIT = ph._rbbox - fld._Lbbox - 1;
                            ph.BYTE = ph._Sbbox;
                        }
                        else
                        {
                            ph.BIT--;
                            ph.field_item++;
                            ph.BYTE += ph.items * fld.size;
                        }
                        for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                            if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0)
                            {
                                ph.BIT -= len_bits;
                                ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT, len_bits);
                                ph.BYTE += ph.items * fld.size;
                            }
                        Debug.Assert((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) == 0);
                        insert(ph, ph.field_bit, len_bits, items * fld.size);
                    }
                    else
                    {
                        int ins_items = item - (ph._wbbox + ph._nbbox) + 1;
                        ph.BIT = ph._Ubbox;
                        ph.BYTE = ph._cbbox;
                        insert(ph, ph.field_bit, ins_items + len_bits, items * fld.size);
                        ph.BIT = ph._Ubbox + len_bits;
                        ph.BYTE = ph._cbbox - items * fld.size;
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    ph.pack.data[ph.BIT >> 3] |= (byte)(1 << (ph.BIT & 7));
                    BitUtils.set_bits((uint)(ph.items = items), len_bits, ph.pack.data, ph.BIT -= len_bits);
                    break;
                case 8:
                case 10:
                    int bits = ph.items * fld.size;
                    if(ph.field_item == item) throw new Exception();
                    if(item < ph._wbbox || ph._nbbox == 0)
                    {
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        ph.BYTE = ph._Sbbox;
                        int ins_items = ph._nbbox == 0 ? 1 : ph._wbbox - item;
                        insert(ph, ph.field_bit, ins_items + bits, 0);
                        ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        BitUtils.set_bits((uint)(ph._wbbox = item), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox + fld._ubbox);
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    else if(item < ph._wbbox + ph._nbbox)
                    {
                        if(item < ph.field_item)
                        {
                            ph.field_item = ph._wbbox;
                            ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        }
                        else
                        {
                            ph.BIT--;
                            ph.field_item++;
                        }
                        for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                            if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0)
                                ph.BIT -= bits;
                        Debug.Assert((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) == 0);
                        insert(ph, ph.field_bit, bits, 0);
                    }
                    else
                    {
                        int ins_items = item + 1 - (ph._wbbox + ph._nbbox);
                        ph.BIT = ph._Ubbox;
                        insert(ph, ph.field_bit, ins_items + bits, 0);
                        ph.BIT = ph._Ubbox + bits;
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    ph.pack.data[ph.BIT >> 3] |= (byte)(1 << (ph.BIT & 7));
                    ph.BIT -= bits;
                    break;
                case 11:
                    if(ph._gbbox - 1 < item) throw new Exception("No room for item=" + item + ". The _gbbox =" + ph._gbbox);
                    if(item < ph.field_item)
                    {
                        ph.field_item = -1;
                        ph.BIT = ph._rbbox - fld._Lbbox;
                    }
                    if(ph.field_item < item)
                        do
                        {
                            bits = fld.size * (int) BitUtils.get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length);
                            ph.BIT -= bits;
                            ph.field_item++;
                        }
                        while(ph.field_item < item);
                    if(BitUtils.get_bits(ph.pack.data, ph.BIT, -fld.length) != 0) throw new Exception("Already allocated");
                    insert(ph, ph.field_bit, items * fld.size, 0);
                    BitUtils.set_bits((uint) items, -fld.length, ph.pack.data, ph.BIT);
                    ph.BIT -= items * fld.size;
                    ph.items = items;
                    break;
                case 12:
                    len_bits = -fld.length;
                    if(item < ph._wbbox || ph._nbbox == 0)
                    {
                        ph.BIT = ph._rbbox - fld._Lbbox;
                        int ins_items = ph._nbbox == 0 ? 1 : ph._wbbox - item;
                        insert(ph, ph.field_bit, ins_items + len_bits + items * fld.size, 0);
                        ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        BitUtils.set_bits((uint)(ph._wbbox = item), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox + fld._ubbox);
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    else if(item < ph._wbbox + ph._nbbox)
                    {
                        if(item < ph.field_item)
                        {
                            ph.field_item = ph._wbbox;
                            ph.BIT = ph._rbbox - fld._Lbbox - 1;
                        }
                        else
                        {
                            ph.BIT--;
                            ph.field_item++;
                        }
                        for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                            if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0)
                            {
                                ph.BIT -= len_bits;
                                ph.items = (int) BitUtils.get_bits(ph.pack.data, ph.BIT, len_bits);
                                ph.BIT -= ph.items * fld.size;
                            }
                        Debug.Assert((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) == 0);
                        insert(ph, ph.field_bit, len_bits + items * fld.size, 0);
                    }
                    else
                    {
                        int ins_items = item - (ph._wbbox + ph._nbbox) + 1;
                        ph.BIT = ph._Ubbox;
                        insert(ph, ph.field_bit, ins_items + len_bits + items * fld.size, 0);
                        ph.BIT = ph._Ubbox + items * fld.size + len_bits;
                        BitUtils.set_bits((uint)(ph._nbbox += ins_items), fld._ubbox, ph.pack.data, ph._rbbox - fld._Lbbox);
                    }
                    ph.pack.data[ph.BIT >> 3] |= (byte)(1 << (ph.BIT & 7));
                    BitUtils.set_bits((uint)(ph.items = items), len_bits, ph.pack.data, ph.BIT -= len_bits);
                    ph.BIT -= items * fld.size;
                    break;
            }
            ph.field_item = item;
        }



