                private static readonly ushort[] tab = {0, 4129, 8258, 12387, 16516, 20645, 24774, 28903, 33032, 37161, 41290, 45419, 49548, 53677, 57806, 61935};


                private static ushort crc16(byte data, ushort crc)
                {
                    crc = (ushort)(tab[(crc >> 12 ^ data >> 4) & 0x0F] ^ crc << 4);
                    return (ushort)(tab[(crc >> 12 ^ data & 0x0F) & 0x0F] ^ crc << 4);
                }

                internal const byte BR = 0x55;

                public override void Write(byte[] src, int BYTE, int bytes)
                {
                    time = ~0;
                    if(time == 0)
                    {
                        time = ~0;
                        b2p.state = Flow.STATE.STANDBY;
                        if(b2p.pack != null) b2p.pack = null;
                    }
                    for(int t; 0 < bytes--; t = b2p.mode != Flow.MODE.CRC ? b2p.crc = crc16(src[BYTE], b2p.crc) : 0, BYTE++)
                    {
                        switch(b2p.state)
                        {
                            case Flow.STATE.STANDBY:
                                b2p.crc = 0;
                                b2p._fbbox = 0;
                                bits = 0;
                                if(src[BYTE] == BR) b2p.state = Flow.STATE.PACK_ID;
                                continue;
                            case Flow.STATE.PACK_ID:
                                if(src[BYTE] == BR)
                                {
                                    failure(" After BR expect helper ID but got +BR");
                                    b2p.state = Flow.STATE.STANDBY;
                                    continue;
                                }
                                b2p._fbbox = (b2p._fbbox << 8) | src[BYTE];
                                if(++bits < pack_id_bytes) continue;
                                b2p.mode = Flow.MODE.NONE;
                                Debug.Assert(b2p.pack == null);
                                if((b2p.pack = process(null, (int) b2p._fbbox)) == null)
                                {
                                    failure("Unrecognized package ID = " + b2p._fbbox);
                                    b2p.state = Flow.STATE.STANDBY;
                                    continue;
                                }
                                b2p._fbbox = 0;
                                bits = 0;
                                b2p.field_bit = -1;
                                b2p._Ubbox = b2p._rbbox = b2p._cbbox = b2p._Sbbox = b2p.field_size = 0;
                                if(b2p.pack.meta.fields != null)
                                {
                                    b2p.state = Flow.STATE.VARINT;
                                    b2p.mode = Flow.MODE.OPTS_INFO;
                                    continue;
                                }
                                break;
                            case Flow.STATE.BYTES:
                                if(src[BYTE] == BR)
                                {
                                    b2p.state = Flow.STATE.BYTES_BR;
                                    continue;
                                }
                            goto case Flow.STATE.BYTES_BR;
                            case Flow.STATE.BYTES_BR:
                                if(b2p.state == Flow.STATE.BYTES_BR)
                                {
                                    if(src[BYTE] != BR)
                                    {
                                        failure("waiting for second BR but got " + src[BYTE]);
                                        b2p.state = Flow.STATE.STANDBY;
                                        continue;
                                    }
                                    b2p.state = Flow.STATE.BYTES;
                                }
                                if(b2p.mode == Flow.MODE.CRC)
                                    switch(b2p.field_size)
                                    {
                                        case 2:
                                            b2p._fbbox = (ulong)(src[BYTE] << 8);
                                            b2p.field_size = 1;
                                            continue;
                                        case 1:
                                            if((b2p._fbbox | src[BYTE]) == b2p.crc) process(b2p.pack, PROCESS_CHANNEL_REQEST);
                                            else failure("CRC error");
                                            b2p.state = Flow.STATE.STANDBY;
                                            b2p.mode = Flow.MODE.NONE;
                                            b2p.pack = null;
                                            continue;
                                    }
                                b2p.pack.data[b2p._Sbbox++] = src[BYTE];
                                if(b2p._Sbbox < b2p._cbbox) continue;
                                break;
                            case Flow.STATE.VARINT:
                                if(src[BYTE] == BR)
                                {
                                    b2p.state = Flow.STATE.VARINT_BR;
                                    continue;
                                }
                            goto case Flow.STATE.VARINT_BR;
                            case Flow.STATE.VARINT_BR:
                                if(b2p.state == Flow.STATE.VARINT_BR)
                                {
                                    if(src[BYTE] != BR)
                                    {
                                        failure("waiting for second BR but got " + src[BYTE]);
                                        b2p.state = Flow.STATE.STANDBY;
                                        continue;
                                    }
                                    b2p.state = Flow.STATE.VARINT;
                                }
                                b2p._fbbox |= (src[BYTE] & 0x7FUL) << bits;
                                bits += 7;
                                if((src[BYTE] & 0x80) != 0) continue;
                                bits = 0;
                                if(b2p.mode == Flow.MODE.OPTS_INFO)
                                {
                                    b2p.pack.data = new byte[b2p.pack.meta.packMinBytes + (int) b2p._fbbox];
                                    b2p._fbbox = 0;
                                    b2p.mode = Flow.MODE.NONE;
                                    break;
                                }
                                BitUtils.set_bytes(b2p._fbbox, b2p.field_size, b2p.pack.data, b2p._Sbbox);
                                b2p._fbbox = 0;
                                if((b2p._Sbbox += b2p.field_size) < b2p._cbbox) continue;
                                break;
                        }
                        if(b2p.next()) continue;
                        b2p.state = Flow.STATE.BYTES;
                        b2p.mode = Flow.MODE.CRC;
                        b2p.field_size = 2;
                    }
                }

                public override int Read(byte[] dst, int BYTE, int bytes)
                {
                    int fix = BYTE;
                    for(int t; 0 < bytes--; t = p2b.mode != Flow.MODE.CRC ? p2b.crc = crc16(dst[BYTE], p2b.crc) : 0, BYTE++)
                    {
                        switch(p2b.state)
                        {
                            case Flow.STATE.STANDBY:
                            {
                                if((p2b.pack = process(null, PROCESS_CHANNEL_REQEST)) == null) return BYTE - fix;
                                p2b.state = Flow.STATE.PACK_ID;
                                p2b._fbbox = p2b.pack.meta.id;
                                p2b.field_bit = 8 * (pack_id_bytes - 1);
                                p2b.crc = 0;
                                dst[BYTE] = BR;
                                continue;
                            }
                            case Flow.STATE.PACK_ID:
                                dst[BYTE] = (byte)(p2b._fbbox >> p2b.field_bit);
                                if(-1 < (p2b.field_bit -= 8)) continue;
                                p2b._fbbox = 0;
                                p2b.field_bit = -1;
                                p2b._Ubbox = p2b._rbbox = p2b._cbbox = p2b._Sbbox = p2b.field_size = 0;
                                p2b.mode = Flow.MODE.NONE;
                                if(p2b.pack.meta.fields != null)
                                {
                                    p2b.state = Flow.STATE.VARINT;
                                    Pack.Meta meta = p2b.pack.meta;
                                    byte[] data = p2b.pack.data;
                                    p2b.setPack(p2b.pack);
                                    _hbbox(p2b);
                                    p2b._fbbox = (ulong)(p2b._ybbox + 1 - meta.packMinBytes);
                                    p2b._Ubbox = p2b._rbbox = p2b._cbbox = p2b._Sbbox = p2b.field_size = 0;
                                    continue;
                                }
                                break;
                            case Flow.STATE.BYTES:
                                if(p2b.mode == Flow.MODE.CRC)
                                {
                                    switch(p2b.field_size)
                                    {
                                        case 4:
                                            p2b.field_size = (dst[BYTE] = (byte)(p2b.crc >> 8)) == BR ? 3 : 2;
                                            continue;
                                        case 3:
                                            dst[BYTE] = BR;
                                            p2b.field_size = 2;
                                            continue;
                                        case 2:
                                            if((dst[BYTE] = (byte)(p2b.crc & 0xFF)) != BR) break;
                                            p2b.field_size = 1;
                                            continue;
                                        case 1:
                                            dst[BYTE] = BR;
                                            break;
                                    }
                                    p2b.state = Flow.STATE.STANDBY;
                                    p2b.mode = Flow.MODE.NONE;
                                    p2b.pack = null;
                                    continue;
                                }
                                if((p2b._fbbox = dst[BYTE] = p2b.pack.data[p2b._Sbbox++]) == BR)
                                {
                                    p2b.state = Flow.STATE.BYTES_BR;
                                    continue;
                                }
                            goto case Flow.STATE.BYTES_BR;
                            case Flow.STATE.BYTES_BR:
                                p2b.state = Flow.STATE.BYTES;
                                dst[BYTE] = (byte) p2b._fbbox;
                                if(p2b._Sbbox < p2b._cbbox) continue;
                                break;
                            case Flow.STATE.VARINT:
                                if((p2b._fbbox & ~0x7FUL) != 0)
                                {
                                    dst[BYTE] = (byte)(p2b._fbbox & 0x7F | 0x80);
                                    p2b._fbbox >>= 7;
                                    continue;
                                }
                                if(p2b._fbbox == BR)
                                {
                                    p2b.state = Flow.STATE.VARINT_BR;
                                    dst[BYTE] = BR;
                                    continue;
                                }
                            goto case Flow.STATE.VARINT_BR;
                            case Flow.STATE.VARINT_BR:
                                p2b.state = Flow.STATE.VARINT;
                                dst[BYTE] = (byte) p2b._fbbox;
                                if((p2b._Sbbox += p2b.field_size) < p2b._cbbox)
                                {
                                    p2b._fbbox = BitUtils.get_bytes(p2b.pack.data, p2b._Sbbox, p2b.field_size);
                                    continue;
                                }
                                break;
                        }
                        if(p2b.next())
                        {
                            if(p2b.state == Flow.STATE.VARINT) p2b._fbbox = BitUtils.get_bytes(p2b.pack.data, p2b._Sbbox, p2b.field_size);
                            continue;
                        }
                        p2b.state = Flow.STATE.BYTES;
                        p2b.mode = Flow.MODE.CRC;
                        p2b.field_size = 4;
                    }
                    return BYTE - fix;
                }
            }

