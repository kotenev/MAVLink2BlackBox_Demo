    static void insert(Pack.Meta.Field.Bounds.Inside ph, final int fbit, final int bits, final int bytes)
    {
        if(ph.field_bit != fbit)
        {
            ph.BIT = ph._Ybbox = ph._ibbox;
            ph.BYTE = ph._Ibbox = ph._lbbox;
        }
        final Pack.Meta meta = ph.pack.meta;
        if(ph._Bbbox < 0) _ybbox(ph);
        int add_to_bits_bytes;
        if(ph.BIT == ph._qbbox && ph.BYTE == ph._Bbbox)
        {
            add_to_bits_bytes = meta._vbbox == 0 ? 0 : bits2bytes(fbit + bits - ph._qbbox + 1);
            if(0 < add_to_bits_bytes || 0 < bytes)
            {
                final int old_pack_data_bytes = ph._Bbbox;
                assert(old_pack_data_bytes == ph.pack.data.length);
                final byte[] data = new byte[old_pack_data_bytes + add_to_bits_bytes + bytes];
                if(ph.field_bit == -1 || bits == 0)
                    System.arraycopy(ph.pack.data, 0, data, 0, old_pack_data_bytes);
                else
                {
                    copy_bits(ph.pack.data, 0,
                              ph.field_bit + 1,
                              data, 0);
                    copy_bits(ph.pack.data, ph.BIT,
                              (old_pack_data_bytes << 3) - ph.BIT, data, ph.BIT + (add_to_bits_bytes << 3));
                }
                ph._Bbbox += add_to_bits_bytes + bytes;
                ph.pack.data = data;
            }
        }
        else
        {
            final int add_bits_bits = meta._vbbox == 0 ? 0 : bits - (ph._qbbox - (ph._Nbbox + 1));
            add_to_bits_bytes = meta._vbbox == 0 ? 0 : bits2bytes(add_bits_bits);
            if(0 < add_to_bits_bytes || 0 < bytes)
            {
                final int old_pack_data_bytes = ph._Bbbox ;
                assert(old_pack_data_bytes == ph.pack.data.length);
                final byte[] data = new byte[old_pack_data_bytes + add_to_bits_bytes + bytes];
                if(bits == 0)
                    System.arraycopy(ph.pack.data, 0, data, 0, ph.BYTE + 1);
                else
                {
                    copy_bits(ph.pack.data, 0,
                              ph._Nbbox + 1,
                              data, 0);
                    copy_bits(ph.pack.data, ph._qbbox,
                              ph.BIT - ph._qbbox, data, ph._qbbox + (add_to_bits_bytes << 3) - bits);
                    copy_bits(ph.pack.data, ph.BIT, (ph.BYTE << 3) - ph.BIT, data, ph.BIT + (add_to_bits_bytes << 3));
                }
                System.arraycopy(
                    ph.pack.data, ph.BYTE, data, ph.BYTE + add_to_bits_bytes + bytes, ph._Bbbox - ph.BYTE);
                ph._Bbbox += add_to_bits_bytes + bytes;
                ph.pack.data = data;
            }
            else
            {
                copy_bits(ph.pack.data, ph._qbbox,
                          ph.BIT - ph._qbbox, ph.pack.data, ph._qbbox - bits);
                set_0(ph.pack.data, ph.BIT - bits, bits);
            }
        }
        if(0 < meta._vbbox && 0 < add_to_bits_bytes)
        {
            int old_value = (int) get_bits(ph.pack.data, meta._pbbox - meta._vbbox, meta._vbbox);
            set_bits(old_value + add_to_bits_bytes, meta._vbbox, ph.pack.data, meta._pbbox - meta._vbbox);
        }
        final int zazor_delta = (add_to_bits_bytes << 3) - bits;
        ph._qbbox += zazor_delta;
        ph.BIT += add_to_bits_bytes << 3;
        ph.BYTE += add_to_bits_bytes;
        if(fbit == ph.field_bit)
        {
            ph._ibbox += zazor_delta;
            ph._Ybbox += add_to_bits_bytes << 3;
            ph._Ibbox += add_to_bits_bytes;
            ph._lbbox += add_to_bits_bytes + bytes;
        }
        else
        {
            ph._Ybbox = ph._ibbox = ph.BIT;
            ph._lbbox = ph._Ibbox = ph.BYTE;
            ph.pack.data[fbit >> 3] |= 1 << (fbit & 7);
            if(ph._Nbbox < fbit) ph._Nbbox = fbit;
            ph.field_bit = fbit;
            ph.field = meta.fields[ph.field_bit - meta._pbbox];
        }
    }

    static boolean visit_field(Pack.Meta.Field.Bounds.Inside ph, int fbit)
    {
        if(ph.field_bit == fbit) return true;
        final Pack.Meta meta = ph.pack.meta;
        final byte[]    data = ph.pack.data;
        int             delta;
        for(; ;)
            if(ph.field_bit == -1)
            {
                if((delta = first_1(data, meta._pbbox, ph._ibbox - meta._pbbox, false)) == -1 ||
                        fbit < meta._pbbox + delta) return false;
                ph.field_bit = meta._pbbox - 1;
                break;
            }
            else if(fbit < ph.field_bit) ph.setPack(ph.pack);
            else
            {
                if(ph.field_bit - meta._pbbox == meta.fields.length - 1 ||
                        (delta = first_1(data, ph.field_bit + 1, ph._ibbox - (ph.field_bit + 1), false)) == -1 ||
                        fbit < ph.field_bit + 1 + delta) return false;
                break;
            }
        for(; ;)
        {
            final Pack.Meta.Field fld = meta.fields[(ph.field_bit += 1 + delta) - meta._pbbox];
            ph.field = fld;
            ph._Ybbox = ph._ibbox;
            ph._Ibbox = ph._lbbox;
            if(0 < fld.length) if(0 < fld.size) ph._lbbox += fld._Kbbox * fld.length * fld.size;
                else ph._ibbox += fld._Kbbox * fld.length * fld.size;
            else setBounds(fld, ph);
            if(ph.field_bit == fbit || ph.field_bit - meta._pbbox == meta.fields.length - 1 ||
                    (delta = first_1(data, ph.field_bit + 1, ph._ibbox - (ph.field_bit + 1), false)) == -1 ||
                    fbit < ph.field_bit + 1 + delta)
            {
                ph.items = fld.length;
                ph.field_size = fld.size;
                ph.BIT = ph._ibbox;
                ph.BYTE = ph._Ibbox;
                if(fld.length < 1)
                {
                    ph._Tbbox = fld._Kbbox;
                    if(0 < fld._Gbbox)
                    {
                        if(ph.D == null || ph.D.length < fld.dims.length) ph.D = new int[fld.dims.length];
                        else for(int i = ph.D.length - 1; -1 < i;) ph.D[i--] = 0;
                        for(int i = 0, bit = ph._Ybbox, dim; i < fld.dims.length; i++)
                            if((dim = fld.dims[i]) < 1) ph._Tbbox *= ph.D[i] = (int) get_bits(data, bit -= -dim, -dim);
                            else ph.D[i] = dim;
                    }
                    init(fld, ph);
                }
                return ph.field_bit == fbit;
            }
        }
    }


    static void init(Pack.Meta.Field fld, Pack.Meta.Field.Bounds.Inside ph)
    {
        switch(fld.type)
        {
            case 1:
                ph.items = -fld.length;
                ph.BIT = ph._ibbox;
                ph.BYTE = ph._Ibbox;
                break;
            case 2:
            case 6:
            case 8:
            case 12:
                ph.items = -fld.length;
                ph.BYTE = ph._Ibbox;
                ph.BIT = ph._Ybbox - fld._Fbbox;
                ph.field_item = Integer.MAX_VALUE;
                ph._tbbox = (int) get_bits(ph.pack.data, ph.BIT, fld._fbbox);
                ph._obbox = (int) get_bits(ph.pack.data, ph.BIT + fld._fbbox, fld._fbbox);
                break;
            case 3:
                ph.BYTE = ph._Ibbox;
                ph.items = (int) get_bits(ph.pack.data, (ph.BIT = ph._Ybbox - fld._Fbbox), -fld.length);
                break;
            case 4:
                ph.items = (int) get_bits(ph.pack.data, (ph.BIT = ph._Ybbox - fld._Fbbox) + 2 * fld._fbbox, -fld.length);
                ph.BYTE = ph._Ibbox;
                ph.field_item = Integer.MAX_VALUE;
                ph._tbbox = (int) get_bits(ph.pack.data, ph.BIT, fld._fbbox);
                ph._obbox = (int) get_bits(ph.pack.data, ph.BIT + fld._fbbox, fld._fbbox);
                break;
            case 5:
            case 11:
                ph.items = 0;
                ph.BYTE = ph._Ibbox;
                ph.BIT = ph._Ybbox - fld._Fbbox;
                ph._obbox = 0;
                ph.field_item = Integer.MAX_VALUE;
                ph._tbbox = ph._Tbbox;
                break;
            case 7:
                ph.items = -fld.length;
                ph.BYTE = ph._Ibbox;
                ph._obbox = 0;
                ph.BIT = ph._ibbox;
                ph.field_item = (ph._tbbox = ph._Tbbox) == 0 ? Integer.MAX_VALUE : 0;
                break;
            case 9:
                ph.items = (int) get_bits(ph.pack.data, (ph.BIT = ph._Ybbox - fld._Fbbox), -fld.length);
                ph.BYTE = ph._Ibbox;
                ph._obbox = 0;
                ph.BIT = ph._ibbox;
                ph.field_item = (ph._tbbox = ph._Tbbox) == 0 ? Integer.MAX_VALUE : 0;
                break;
            case 10:
                ph.items = (int) get_bits(ph.pack.data, (ph.BIT = ph._Ybbox - fld._Fbbox) + 2 * fld._fbbox, -fld.length);
                ph.BYTE = ph._Ibbox;
                ph.field_item = Integer.MAX_VALUE;
                ph._tbbox = (int) get_bits(ph.pack.data, ph.BIT, fld._fbbox);
                ph._obbox = (int) get_bits(ph.pack.data, ph.BIT + fld._fbbox, fld._fbbox);
                break;
            default:
                assert(false);
        }
    }

    static void setBounds(Pack.Meta.Field fld, Pack.Meta.Field.Bounds ph)
    {
        ph.field_size = fld.size;
        switch(fld.type)
        {
            case 1:
                int count = 1;
                if(0 < fld._Gbbox) for(int i = 0, bit = ph._ibbox; i < fld.dims.length; i++) if(fld.dims[i] < 0) count *= get_bits(ph.pack.data, bit -= -fld.dims[i], -fld.dims[i]);
                ph._ibbox -= fld._Fbbox;
                ph._lbbox += fld._Kbbox * count * -fld.length * fld.size;
                break;
            case 2:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                ph.field_size = fld.size;
                int bit = ph._ibbox;
                ph._ibbox -= count;
                byte[] data = ph.pack.data;
                while(ph._ibbox < bit--) if((data[bit >>> 3] & 1 << (bit & 7)) == 0) count--;
                ph._lbbox += -fld.length * count * fld.size;
                break;
            case 3:
                ph.field_size = fld.size;
                bit = ph._ibbox;
                ph._ibbox -= fld._Fbbox;
                int arrays_length = (int) get_bits(ph.pack.data, ph._ibbox, -fld.length);
                if(0 < fld._Gbbox) for(int i = 0; i < fld.dims.length; i++) if(fld.dims[i] < 0) arrays_length *= get_bits(ph.pack.data, bit -= -fld.dims[i], -fld.dims[i]);
                ph._lbbox += fld._Kbbox * arrays_length * fld.size;
                break;
            case 4:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                ph.field_size = fld.size;
                bit = ph._ibbox;
                ph._ibbox -= count;
                data = ph.pack.data;
                arrays_length = (int) get_bits(data, bit + 2 * fld._fbbox, -fld.length);
                while(ph._ibbox < bit--) if((data[bit >>> 3] & 1 << (bit & 7)) == 0) count--;
                ph._lbbox += count * arrays_length * fld.size;
                break;
            case 5:
                ph.field_size = fld.size;
                count = fld._Kbbox;
                if(0 < fld._Gbbox) for(int i = 0, _bit = ph._ibbox; i < fld.dims.length; i++) if(fld.dims[i] < 0) count *= get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                ph._ibbox -= fld._Fbbox;
                int all_arrays_sum = 0;
                while(0 < count--) all_arrays_sum += get_bits(ph.pack.data, ph._ibbox -= -fld.length, -fld.length);
                ph._lbbox += all_arrays_sum * fld.size;
                break;
            case 6:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                ph.field_size = fld.size;
                bit = ph._ibbox;
                all_arrays_sum = 0;
                data = ph.pack.data;
                while(0 < count--) if((data[--bit >> 3] & 1 << (bit & 7)) != 0) all_arrays_sum += get_bits(ph.pack.data, bit -= -fld.length, -fld.length);
                ph._ibbox = bit;
                ph._lbbox += all_arrays_sum * fld.size;
                break;
            case 7:
                ph.field_size = 0;
                count = 1;
                if(0 < fld._Gbbox) for(int i = 0, _bit = ph._ibbox; i < fld.dims.length; i++) if(fld.dims[i] < 0) count *= get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                ph._ibbox -= fld._Fbbox + count * fld._Kbbox * -fld.length * fld.size;
                break;
            case 8:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                bit = ph._ibbox;
                ph.field_size = fld.size;
                data = ph.pack.data;
                for(final int bits = -fld.length * fld.size; 0 < count--;)
                    if((data[--bit >> 3] & 1 << (bit & 7)) != 0) bit -= bits;
                ph._ibbox = bit;
                break;
            case 9:
                ph.field_size = fld.size;
                bit = ph._ibbox;
                ph._ibbox -= fld._Fbbox;
                arrays_length = (int) get_bits(ph.pack.data, ph._ibbox, -fld.length);
                if(0 < fld._Gbbox) for(int i = 0; i < fld.dims.length; i++) if(fld.dims[i] < 0) arrays_length *= get_bits(ph.pack.data, bit -= -fld.dims[i], -fld.dims[i]);
                ph._ibbox -= fld._Kbbox * arrays_length * fld.size;
                break;
            case 10:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                bit = ph._ibbox;
                ph.field_size = fld.size;
                data = ph.pack.data;
                for(final int bits = (int) get_bits(data, bit + 2 * fld._fbbox, -fld.length) * fld.size; 0 < count--;)
                    if((data[--bit >> 3] & 1 << (bit & 7)) != 0) bit -= bits;
                ph._ibbox = bit;
                break;
            case 11:
                ph.field_size = fld.size;
                count = fld._Kbbox;
                if(0 < fld._Gbbox) for(int i = 0, _bit = ph._ibbox; i < fld.dims.length; i++) if(fld.dims[i] < 0) count *= get_bits(ph.pack.data, _bit -= -fld.dims[i], -fld.dims[i]);
                ph._ibbox -= fld._Fbbox;
                while(0 < count--)
                {
                    int bits = (int)(get_bits(ph.pack.data, ph._ibbox -= -fld.length, -fld.length) * fld.size);
                    ph._ibbox -= bits;
                }
                break;
            case 12:
                count = (int) get_bits(ph.pack.data, ph._ibbox -= fld._Fbbox, fld._fbbox);
                if(count == 0) return;
                ph.field_size = fld.size;
                bit = ph._ibbox;
                all_arrays_sum = 0;
                data = ph.pack.data;
                while(0 < count--) if((data[--bit >> 3] & 1 << (bit & 7)) != 0)
                    {
                        int len = (int) get_bits(ph.pack.data, bit -= -fld.length, -fld.length);
                        bit -= len * fld.size;
                    }
                ph._ibbox = bit;
                break;
            default:
                assert(false);
        }
    }


    static void _ybbox(Pack.Meta.Field.Bounds ph)
    {
        final Pack.Meta meta = ph.pack.meta;
        ph._Nbbox = ph.field_bit == -1 ? meta._pbbox - 1 : ph.field_bit;
        int delta = ph.field_bit == -1 ? first_1(ph.pack.data, meta._pbbox, ph._ibbox - meta._pbbox, false) : first_1(ph.pack.data, ph.field_bit + 1, ph._ibbox - (ph.field_bit + 1), false);
        if(delta == -1)
        {
            ph._Bbbox = ph._lbbox;
            ph._Nbbox = ph.field_bit;
            ph._qbbox = ph._ibbox;
            assert(ph._Bbbox == (ph.pack.data.length));
        }
        else
        {
            final int _ibbox_FX = ph._ibbox, _Ybbox_FX = ph._Ybbox, _Ibbox_FX = ph._Ibbox, _lbbox_FX = ph._lbbox, item_size_FX = ph.field_size;
            for(int max = meta._pbbox + meta.fields.length - 1; ;)
            {
                ph._Nbbox += delta + 1;
                ph._Ybbox = ph._ibbox;
                ph._Ibbox = ph._lbbox;
                final Pack.Meta.Field fld = meta.fields[ph._Nbbox - meta._pbbox];
                if(0 < fld.length) if(0 < fld.size) ph._lbbox += fld._Kbbox * fld.length * fld.size;
                    else ph._ibbox += fld._Kbbox * fld.length * fld.size;
                else setBounds(fld, ph);
                if(ph._Nbbox == max || (delta = first_1(ph.pack.data, ph._Nbbox + 1, ph._ibbox - (ph._Nbbox + 1), false)) == -1) break;
            }
            ph._Bbbox = ph._lbbox;
            ph._qbbox = ph._ibbox;
            assert(ph._Bbbox == ph.pack.data.length);
            ph._ibbox = _ibbox_FX;
            ph._Ybbox = _Ybbox_FX;
            ph._Ibbox = _Ibbox_FX;
            ph._lbbox = _lbbox_FX;
            ph.field_size = item_size_FX;
        }
    }

    public static boolean insert_field(Pack.Meta.Field.Bounds.Inside ph, int fbit, int items, int... var_dims)
    {
        if(visit_field(ph, fbit)) return false;
        Pack.Meta.Field fld = ph.pack.meta.fields[fbit - ph.pack.meta._pbbox];
        if(fld.type == 0)
        {
            if(visit_field(ph, fbit)) return false;
            if(0 < fld.size)
            {
                insert(ph, fbit, 0, fld._Kbbox * fld.length * fld.size);
                ph._lbbox = (ph.BYTE = ph._Ibbox) + fld._Kbbox * (ph.items = fld.length) * (ph.field_size = fld.size);
            }
            else
            {
                insert(ph, fbit, fld._Kbbox * fld.length * -fld.size, 0);
                ph.BIT = ph._ibbox = ph._Ybbox + fld._Kbbox * (ph.items = fld.length) * fld.size;
            }
            return true;
        }
        int total = fld._Kbbox;
        if(var_dims != null)
            for(int i = var_dims.length - 1; -1 < i; i--) total *= var_dims[i];
        switch(fld.type)
        {
            case 1:
                insert(ph, fbit, fld._Fbbox, total * -fld.length * fld.size);
                break;
            case 2:
            case 6:
            case 8:
            case 12:
                insert(ph, fbit, fld._Fbbox, 0);
                break;
            case 3:
                insert(ph, fbit, fld._Fbbox, total * items * fld.size);
                set_bits(items, -fld.length, ph.pack.data, ph._Ybbox - fld._Fbbox);
                break;
            case 4:
                insert(ph, fbit, fld._Fbbox, 0);
                set_bits(items, -fld.length, ph.pack.data, ph._Ybbox - fld._Fbbox + 2 * fld._fbbox);
                break;
            case 5:
            case 11:
                insert(ph, fbit, fld._Fbbox + total * -fld.length, 0);
                break;
            case 7:
                insert(ph, fbit, fld._Fbbox + total * -fld.length * fld.size, 0);
                break;
            case 9:
                insert(ph, fbit, fld._Fbbox + total * items * fld.size, 0);
                set_bits(items, -fld.length, ph.pack.data, ph._Ybbox - fld._Fbbox);
                break;
            case 10:
                insert(ph, fbit, fld._Fbbox, 0);
                set_bits(items, -fld.length, ph.pack.data, ph._Ybbox - fld._Fbbox + 2 * fld._fbbox);
                break;
            default:
                assert(false);
        }
        ph._Tbbox = total;
        if(0 < fld._Gbbox)
        {
            if(ph.D == null || ph.D.length < fld.dims.length) ph.D = new int[fld.dims.length];
            else for(int i = ph.D.length - 1; -1 < i;) ph.D[i--] = 0;
            for(int i = 0, k = 0, bit = ph._Ybbox; i < fld.dims.length; i++)
                if(fld.dims[i] < 1)
                {
                    ph.D[i] = var_dims[k];
                    set_bits(var_dims[k], -fld.dims[i], ph.pack.data, bit -= -fld.dims[i]);
                    k++;
                }
                else ph.D[i] = fld.dims[i];
        }
        setBounds(fld, ph);
        init(fld, ph);
        return true;
    }

    public static boolean try_visit_item(Pack.Meta.Field.Bounds.Inside ph, int item)
    {
        final Pack.Meta.Field fld = ph.field;
        switch(fld.type)
        {
            default:
                assert(false);
                return false;
            case 2:
            case 4:
                if(ph.field_item == item) return true;
                if(ph._tbbox == 0 || item < ph._obbox || ph._obbox + ph._tbbox <= item) return false;
                int bit = ph._Ybbox - fld._Fbbox - 1 - (item - ph._obbox);
                if((ph.pack.data[bit >> 3] & 1 << (bit & 7)) == 0) return false;
                if(item < ph.field_item)
                {
                    ph.field_item = ph._obbox;
                    ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    ph.BYTE = ph._Ibbox;
                }
                else
                {
                    ph.BIT--;
                    ph.field_item++;
                    ph.BYTE += ph.items * fld.size;
                }
                for(final int bytes = ph.items * fld.size; ; ph.field_item++, ph.BIT--)
                    if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0) if(ph.field_item == item) break;
                        else ph.BYTE += bytes;
                return true;
            case 5:
                if(ph._Tbbox - 1 < item || get_bits(ph.pack.data, ph._Ybbox - fld._Fbbox - (item + 1) * -fld.length, -fld.length) == 0) return false;
                if(item < ph.field_item)
                {
                    ph.field_item = 0;
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    ph.items = (int) get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length);
                    ph.BYTE = ph._Ibbox;
                }
                for(; ph.field_item < item; ph.BYTE += ph.items * fld.size, ph.items = (int) get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length), ph.field_item++) ;
                return true;
            case 6:
                if(ph.field_item == item) return true;
                if(item < ph._obbox || ph._obbox + ph._tbbox <= item) return false;
                int field_item = ph.field_item;
                bit = ph.BIT;
                int BYTE = ph.BYTE;
                if(item < field_item)
                {
                    field_item = ph._obbox;
                    bit = ph._Ybbox - fld._Fbbox - 1;
                    BYTE = ph._Ibbox;
                }
                else
                {
                    bit--;
                    field_item++;
                    BYTE += ph.items * fld.size;
                }
                for(final int len_bits = -fld.length; ; bit--, field_item++)
                    if((ph.pack.data[bit >>> 3] & 1 << (bit & 7)) != 0)
                    {
                        bit -= len_bits;
                        if(field_item == item)
                        {
                            ph.field_item = field_item;
                            ph.BYTE = BYTE;
                            ph.BIT = bit;
                            ph.items = (int) get_bits(ph.pack.data, bit, len_bits);
                            return true;
                        }
                        BYTE += get_bits(ph.pack.data, bit, len_bits) * fld.size;
                    }
                    else if(field_item == item) return false;
            case 8:
            case 10:
                if(ph.field_item == item) return true;
                if(item < ph._obbox || ph._obbox + ph._tbbox <= item) return false;
                field_item = ph.field_item;
                bit = ph.BIT;
                if(item < field_item)
                {
                    field_item = ph._obbox;
                    bit = ph._Ybbox - fld._Fbbox - 1;
                }
                else
                {
                    bit--;
                    field_item++;
                }
                for(int bits = ph.items * fld.size; ; bit--, field_item++)
                    if((ph.pack.data[bit >>> 3] & 1 << (bit & 7)) != 0)
                    {
                        bit -= bits;
                        if(field_item == item)
                        {
                            ph.field_item = item;
                            ph.BIT = bit;
                            return true;
                        }
                    }
                    else if(field_item == item) return false;
            case 11:
                if(ph._Tbbox - 1 < item) return false;
                field_item = ph.field_item;
                bit = ph.BIT;
                if(item < field_item)
                {
                    field_item = -1;
                    bit = ph._Ybbox - fld._Fbbox;
                }
                else if(item == ph.field_item) return true;
                int items;
                do
                {
                    int bits = fld.size * (items = (int) get_bits(ph.pack.data, bit -= -fld.length, -fld.length));
                    bit -= bits;
                    field_item++;
                }
                while(field_item < item);
                if(items == 0) return false;
                ph.BIT = bit;
                ph.items = items;
                ph.field_item = field_item;
                return true;
            case 12:
                if(ph.field_item == item) return true;
                if(item < ph._obbox || ph._obbox + ph._tbbox <= item) return false;
                field_item = ph.field_item;
                bit = ph.BIT;
                if(item < field_item)
                {
                    field_item = ph._obbox;
                    bit = ph._Ybbox - fld._Fbbox - 1;
                }
                else
                {
                    bit--;
                    field_item++;
                }
                for(final int len_bits = -fld.length; ; bit--, field_item++)
                    if((ph.pack.data[bit >>> 3] & 1 << (bit & 7)) != 0)
                    {
                        bit -= len_bits;
                        if(field_item == item)
                        {
                            ph.field_item = field_item;
                            ph.items = (int) get_bits(ph.pack.data, bit, len_bits);
                            ph.BIT = bit - ph.items * fld.size;
                            return true;
                        }
                        bit -= get_bits(ph.pack.data, bit, len_bits) * fld.size;
                    }
                    else if(field_item == item) return false;
        }
    }


    public static void insert_item(Pack.Meta.Field.Bounds.Inside ph, int item, int items)
    {
        final Pack.Meta.Field fld = ph.field;
        switch(fld.type)
        {
            default:
                assert(false);
                return;
            case 2:
            case 4:
                if(item < ph._obbox || ph._tbbox == 0)
                {
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    ph.BYTE = ph._Ibbox;
                    final int ins_items = ph._tbbox == 0 ? 1 : ph._obbox - item;
                    insert(ph, ph.field_bit, ins_items, ph.items * fld.size);
                    ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    set_bits(ph._obbox = item, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox + fld._fbbox);
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                else if(item < ph._obbox + ph._tbbox)
                {
                    if(item < ph.field_item)
                    {
                        ph.field_item = ph._obbox;
                        ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                        ph.BYTE = ph._Ibbox;
                    }
                    else
                    {
                        ph.BIT--;
                        ph.field_item++;
                        ph.BYTE += ph.items * fld.size;
                    }
                    for(final int bytes = ph.items * fld.size; ph.field_item < item; ph.field_item++, ph.BIT--)
                        if((ph.pack.data[ph.BIT >> 3] & 1 << (ph.BIT & 7)) != 0) ph.BYTE += bytes;
                    assert((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) == 0);
                    insert(ph, ph.field_bit, 0, ph.items * fld.size);
                }
                else
                {
                    final int ins_items = item + 1 - (ph._obbox + ph._tbbox);
                    ph.BIT = ph._ibbox;
                    ph.BYTE = ph._lbbox;
                    insert(ph, ph.field_bit, ins_items, ph.items * fld.size);
                    ph.BIT = ph._ibbox;
                    ph.BYTE = ph._lbbox - ph.items * fld.size;
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                ph.pack.data[ph.BIT >> 3] |= 1 << (ph.BIT & 7);
                break;
            case 5:
                if(ph._Tbbox - 1 < item) throw new RuntimeException("No room for item=" + item + ". The _Tbbox =" + ph._Tbbox);
                if(item < ph.field_item)
                {
                    ph.field_item = 0;
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    ph.BYTE = ph._Ibbox;
                    ph.items = (int) get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length);
                }
                for(; ph.field_item < item; ph.BYTE += ph.items * fld.size, ph.items = (int) get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length), ph.field_item++) ;
                if(ph.items != 0) throw new RuntimeException("Already allocated");
                insert(ph, ph.field_bit, 0, items * fld.size);
                set_bits(items, -fld.length, ph.pack.data, ph.BIT);
                ph.items = items;
                break;
            case 6:
                int len_bits = -fld.length;
                if(item < ph._obbox || ph._tbbox == 0)
                {
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    ph.BYTE = ph._Ibbox;
                    final int ins_items = ph._tbbox == 0 ? 1 : ph._obbox - item;
                    insert(ph, ph.field_bit, ins_items + len_bits, items * fld.size);
                    ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    set_bits(ph._obbox = item, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox + fld._fbbox);
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                else if(item < ph._obbox + ph._tbbox)
                {
                    if(item < ph.field_item)
                    {
                        ph.field_item = ph._obbox;
                        ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                        ph.BYTE = ph._Ibbox;
                    }
                    else
                    {
                        ph.BIT--;
                        ph.field_item++;
                        ph.BYTE += ph.items * fld.size;
                    }
                    for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                        if((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) != 0)
                        {
                            ph.BIT -= len_bits;
                            ph.items = (int) get_bits(ph.pack.data, ph.BIT, len_bits);
                            ph.BYTE += ph.items * fld.size;
                        }
                    assert((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) == 0);
                    insert(ph, ph.field_bit, len_bits, items * fld.size);
                }
                else
                {
                    final int ins_items = item - (ph._obbox + ph._tbbox) + 1;
                    ph.BIT = ph._ibbox;
                    ph.BYTE = ph._lbbox;
                    insert(ph, ph.field_bit, ins_items + len_bits, items * fld.size);
                    ph.BIT = ph._ibbox + len_bits;
                    ph.BYTE = ph._lbbox - items * fld.size;
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                ph.pack.data[ph.BIT >>> 3] |= 1 << (ph.BIT & 7);
                set_bits(ph.items = items, len_bits, ph.pack.data, ph.BIT -= len_bits);
                break;
            case 8:
            case 10:
                int bits = ph.items * fld.size;
                if(ph.field_item == item) throw new RuntimeException();
                if(item < ph._obbox || ph._tbbox == 0)
                {
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    ph.BYTE = ph._Ibbox;
                    int ins_items = ph._tbbox == 0 ? 1 : ph._obbox - item;
                    insert(ph, ph.field_bit, ins_items + bits, 0);
                    ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    set_bits(ph._obbox = item, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox + fld._fbbox);
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                else if(item < ph._obbox + ph._tbbox)
                {
                    if(item < ph.field_item)
                    {
                        ph.field_item = ph._obbox;
                        ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    }
                    else
                    {
                        ph.BIT--;
                        ph.field_item++;
                    }
                    for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                        if((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) != 0) ph.BIT -= bits;
                    assert((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) == 0);
                    insert(ph, ph.field_bit, bits, 0);
                }
                else
                {
                    final int ins_items = item + 1 - (ph._obbox + ph._tbbox);
                    ph.BIT = ph._ibbox;
                    insert(ph, ph.field_bit, ins_items + bits, 0);
                    ph.BIT = ph._ibbox + bits;
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                ph.pack.data[ph.BIT >>> 3] |= 1 << (ph.BIT & 7);
                ph.BIT -= bits;
                break;
            case 11:
                if(ph._Tbbox - 1 < item) throw new RuntimeException("No room for item=" + item + ". The _Tbbox =" + ph._Tbbox);
                if(item < ph.field_item)
                {
                    ph.field_item = -1;
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                }
                if(ph.field_item < item) do
                    {
                        bits = (int)(fld.size * get_bits(ph.pack.data, ph.BIT -= -fld.length, -fld.length));
                        ph.BIT -= bits;
                        ph.field_item++;
                    }
                    while(ph.field_item < item);
                if(get_bits(ph.pack.data, ph.BIT, -fld.length) != 0) throw new RuntimeException("Already allocated");
                insert(ph, ph.field_bit, items * fld.size, 0);
                set_bits(items, -fld.length, ph.pack.data, ph.BIT);
                ph.BIT -= items * fld.size;
                ph.items = items;
                break;
            case 12:
                len_bits = -fld.length;
                if(item < ph._obbox || ph._tbbox == 0)
                {
                    ph.BIT = ph._Ybbox - fld._Fbbox;
                    final int ins_items = ph._tbbox == 0 ? 1 : ph._obbox - item;
                    insert(ph, ph.field_bit, ins_items + len_bits + items * fld.size, 0);
                    ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    set_bits(ph._obbox = item, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox + fld._fbbox);
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                else if(item < ph._obbox + ph._tbbox)
                {
                    if(item < ph.field_item)
                    {
                        ph.field_item = ph._obbox;
                        ph.BIT = ph._Ybbox - fld._Fbbox - 1;
                    }
                    else
                    {
                        ph.BIT--;
                        ph.field_item++;
                    }
                    for(; ph.field_item < item; ph.BIT--, ph.field_item++)
                        if((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) != 0)
                        {
                            ph.BIT -= len_bits;
                            ph.items = (int) get_bits(ph.pack.data, ph.BIT, len_bits);
                            ph.BIT -= ph.items * fld.size;
                        }
                    assert((ph.pack.data[ph.BIT >>> 3] & 1 << (ph.BIT & 7)) == 0);
                    insert(ph, ph.field_bit, len_bits + items * fld.size, 0);
                }
                else
                {
                    final int ins_items = item - (ph._obbox + ph._tbbox) + 1;
                    ph.BIT = ph._ibbox;
                    insert(ph, ph.field_bit, ins_items + len_bits + items * fld.size, 0);
                    ph.BIT = ph._ibbox + items * fld.size + len_bits;
                    set_bits(ph._tbbox += ins_items, fld._fbbox, ph.pack.data, ph._Ybbox - fld._Fbbox);
                }
                ph.pack.data[ph.BIT >>> 3] |= 1 << (ph.BIT & 7);
                set_bits(ph.items = items, len_bits, ph.pack.data, ph.BIT -= len_bits);
                ph.BIT -= items * fld.size;
                break;
        }
        ph.field_item = item;
    }



    public static boolean try_visit_field(Pack.Meta.Field.Bounds.Inside ph, int fbit)
    {
        return !(ph.wasNULL = (ph._ibbox <= fbit || (ph.pack.data[fbit >> 3] & 1 << (fbit & 7)) == 0 || !visit_field(ph, fbit)));
    }


