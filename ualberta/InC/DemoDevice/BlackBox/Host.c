bool insert_field(Bounds_Inside* ph, int32_t fbit, int32_t items, ...)
{
    if(visit_field(ph, fbit)) return false;
    Bounds* ph_ = &ph->base;
    Meta* const meta = ph_->pack->meta;
    Field* fld = meta->fields[fbit - meta->_Hbbox];
    if(fld->type == 0)
    {
        if(0 < fld->size)
        {
            insert(ph, fbit, 0, fld->_ibbox * fld->length * fld->size);
            ph_->_Xbbox = (ph->BYTE = ph_->_kbbox) + fld->_ibbox * (ph->items = fld->length) * (ph_->field_size = fld->
                          size);
        }
        else
        {
            insert(ph, fbit, fld->_ibbox * fld->length * -fld->size, 0);
            ph->BIT = ph_->_Pbbox = ph_->_ebbox + fld->_ibbox * (ph->items = fld->length) * fld->size;
        }
        return true;
    }
    int32_t total = fld->_ibbox;
    if(fld->_obbox)
    {
        va_list ap;
        va_start(ap, items);
        for(int32_t i = fld->_obbox; 0 < i--;)
            total *= va_arg(ap, int);
        va_end(ap);
    }
    switch(fld->type)
    {
        case 1:
            insert(ph, fbit, fld->_wbbox, total * -fld->length * fld->size);
            break;
        case 2:
        case 6:
        case 8:
        case 12:
            insert(ph, fbit, fld->_wbbox, 0);
            break;
        case 3:
            insert(ph, fbit, fld->_wbbox, total * items * fld->size);
            set_bits(items, -fld->length, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            break;
        case 4:
            insert(ph, fbit, fld->_wbbox, 0);
            set_bits(items, -fld->length, ph_->pack->data, ph_->_ebbox - fld->_wbbox + 2 * fld->_Sbbox);
            break;
        case 5:
        case 11:
            insert(ph, fbit, fld->_wbbox + total * -fld->length, 0);
            break;
        case 7:
            insert(ph, fbit, fld->_wbbox + total * -fld->length * fld->size, 0);
            break;
        case 9:
            insert(ph, fbit, fld->_wbbox + total * items * fld->size, 0);
            set_bits(items, -fld->length, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            break;
        case 10:
            insert(ph, fbit, fld->_wbbox, 0);
            set_bits(items, -fld->length, ph_->pack->data, ph_->_ebbox - fld->_wbbox + 2 * fld->_Sbbox);
            break;
    }
    if(fld->_obbox)
    {
        for(int32_t i = DL - 1; -1 < i;) ph->D[i--] = 0;
        va_list ap;
        va_start(ap, items);
        for(int32_t i = 0, bit = ph_->_ebbox; i < fld->_nbbox; i++)
            if(fld->dims[i] < 0)
                set_bits(ph->D[i] = va_arg(ap, int), -fld->dims[i], ph_->pack->data, bit -= -fld->dims[i]);
            else ph->D[i] = fld->dims[i];
        va_end(ap);
    }
    ph->_jbbox = total;
    setBounds(fld, &ph->base);
    init(fld, ph);
    return true;
}


bool try_visit_item(Bounds_Inside* ph, int32_t item)
{
    Bounds* ph_ = &ph->base;
    Field* fld = ph_->field;
    switch(fld->type)
    {
        default:
            return false;
        case 2:
        case 4:
            if(ph->field_item == item) return true;
            if(ph->_cbbox == 0 || item < ph->_Qbbox || ph->_Qbbox + ph->_cbbox <= item) return false;
            int32_t bit = ph_->_ebbox - fld->_wbbox - 1 - (item - ph->_Qbbox);
            if((ph_->pack->data[bit >> 3] & 1 << (bit & 7)) == 0) return false;
            if(item < ph->field_item)
            {
                ph->field_item = ph->_Qbbox;
                ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                ph->BYTE = ph_->_kbbox;
            }
            else
            {
                ph->BIT--;
                ph->field_item++;
                ph->BYTE += ph->items * fld->size;
            }
            for(int32_t bytes = ph->items * fld->size; ; ph->field_item++, ph->BIT--)
                if((ph_->pack->data[ph->BIT >> 3] & 1 << (ph->BIT & 7)) != 0)
                {
                    if(ph->field_item == item) break;
                    ph->BYTE += bytes;
                }
            return true;
        case 5:
            if(ph->_jbbox - 1 < item || get_bits(ph_->pack->data,
                                                 ph_->_ebbox - fld->_wbbox - (item + 1) * -fld->length,
                                                 -fld->length) == 0)
                return false;
            if(item < ph->field_item)
            {
                ph->field_item = 0;
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                ph->items = (int)get_bits(ph_->pack->data, ph->BIT -= -fld->length, -fld->length);
                ph->BYTE = ph_->_kbbox;
            }
            for(; ph->field_item < item; ph->BYTE += ph->items * fld->size, ph->items = (int)get_bits(
                        ph_->pack->data, ph->BIT -= -fld->length, -fld->length), ph->field_item++);
            return true;
        case 6:
            if(ph->field_item == item) return true;
            if(item < ph->_Qbbox || ph->_Qbbox + ph->_cbbox <= item) return false;
            int32_t field_item = ph->field_item;
            bit = ph->BIT;
            int32_t BYTE = ph->BYTE;
            if(item < field_item)
            {
                field_item = ph->_Qbbox;
                bit = ph_->_ebbox - fld->_wbbox - 1;
                BYTE = ph_->_kbbox;
            }
            else
            {
                bit--;
                field_item++;
                BYTE += ph->items * fld->size;
            }
            for(int32_t len_bits = -fld->length; ; bit--, field_item++)
                if((ph_->pack->data[bit >> 3] & 1 << (bit & 7)) != 0)
                {
                    bit -= len_bits;
                    if(field_item == item)
                    {
                        ph->field_item = field_item;
                        ph->BYTE = BYTE;
                        ph->BIT = bit;
                        ph->items = (int)get_bits(ph_->pack->data, bit, len_bits);
                        return true;
                    }
                    BYTE += get_bits(ph_->pack->data, bit, len_bits) * fld->size;
                }
                else if(field_item == item) return false;
        case 8:
        case 10:
            if(ph->field_item == item) return true;
            if(item < ph->_Qbbox || ph->_Qbbox + ph->_cbbox <= item) return false;
            field_item = ph->field_item;
            bit = ph->BIT;
            if(item < field_item)
            {
                field_item = ph->_Qbbox;
                bit = ph_->_ebbox - fld->_wbbox - 1;
            }
            else
            {
                bit--;
                field_item++;
            }
            for(int32_t bits = ph->items * fld->size; ; bit--, field_item++)
                if((ph_->pack->data[bit >> 3] & 1 << (bit & 7)) != 0)
                {
                    bit -= bits;
                    if(field_item == item)
                    {
                        ph->field_item = item;
                        ph->BIT = bit;
                        return true;
                    }
                }
                else if(field_item == item) return false;
        case 11:
            if(ph->_jbbox - 1 < item) return false;
            field_item = ph->field_item;
            bit = ph->BIT;
            if(item < field_item)
            {
                field_item = -1;
                bit = ph_->_ebbox - fld->_wbbox;
            }
            else if(item == ph->field_item) return true;
            int32_t items;
            do
            {
                int32_t bits = fld->size * (items = (int)get_bits(ph_->pack->data, bit -= -fld->length, -fld->length));
                bit -= bits;
                field_item++;
            }
            while(field_item < item);
            if(items == 0) return false;
            ph->BIT = bit;
            ph->items = items;
            ph->field_item = field_item;
            return true;
        case 12:
            if(ph->field_item == item) return true;
            if(item < ph->_Qbbox || ph->_Qbbox + ph->_cbbox <= item) return false;
            field_item = ph->field_item;
            bit = ph->BIT;
            if(item < field_item)
            {
                field_item = ph->_Qbbox;
                bit = ph_->_ebbox - fld->_wbbox - 1;
            }
            else
            {
                bit--;
                field_item++;
            }
            for(int32_t len_bits = -fld->length; ; bit--, field_item++)
                if((ph_->pack->data[bit >> 3] & 1 << (bit & 7)) != 0)
                {
                    bit -= len_bits;
                    if(field_item == item)
                    {
                        ph->field_item = field_item;
                        ph->items = (int)get_bits(ph_->pack->data, bit, len_bits);
                        ph->BIT = bit - ph->items * fld->size;
                        return true;
                    }
                    bit -= get_bits(ph_->pack->data, bit, len_bits) * fld->size;
                }
                else if(field_item == item) return false;
    }
}

void insert_item(Bounds_Inside* ph, int32_t item, const int32_t items)
{
    Bounds* ph_ = &ph->base;
    Field* fld = ph_->field;
    switch(fld->type)
    {
        default:
            return;
        case 2:
        case 4:
            if(item < ph->_Qbbox || ph->_cbbox == 0)
            {
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                ph->BYTE = ph_->_kbbox;
                int32_t ins_items = ph->_cbbox == 0 ? 1 : ph->_Qbbox - item;
                insert(ph, ph_->field_bit, ins_items, ph->items * fld->size);
                ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                set_bits(ph->_Qbbox = item, fld->_Sbbox, ph_->pack->data,
                         ph_->_ebbox - fld->_wbbox + fld->_Sbbox);
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            else if(item < ph->_Qbbox + ph->_cbbox)
            {
                if(item < ph->field_item)
                {
                    ph->field_item = ph->_Qbbox;
                    ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                    ph->BYTE = ph_->_kbbox;
                }
                else
                {
                    ph->BIT--;
                    ph->field_item++;
                    ph->BYTE += ph->items * fld->size;
                }
                for(int32_t bytes = ph->items * fld->size; ph->field_item < item; ph->field_item++, ph->BIT--)
                    if((ph_->pack->data[ph->BIT >> 3] & 1 << (ph->BIT & 7)) != 0) ph->BYTE += bytes;
                insert(ph, ph_->field_bit, 0, ph->items * fld->size);
            }
            else
            {
                int32_t ins_items = item + 1 - (ph->_Qbbox + ph->_cbbox);
                ph->BIT = ph_->_Pbbox;
                ph->BYTE = ph_->_Xbbox;
                insert(ph, ph_->field_bit, ins_items, ph->items * fld->size);
                ph->BIT = ph_->_Pbbox;
                ph->BYTE = ph_->_Xbbox - ph->items * fld->size;
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            ph_->pack->data[ph->BIT >> 3] |= 1 << (ph->BIT & 7);
            break;
        case 5:
            if(item < ph->field_item)
            {
                ph->field_item = 0;
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                ph->BYTE = ph_->_kbbox;
                ph->items = (int)get_bits(ph_->pack->data, ph->BIT -= -fld->length, -fld->length);
            }
            for(; ph->field_item < item; ph->BYTE += ph->items * fld->size, ph->items = (int)get_bits(
                        ph_->pack->data, ph->BIT -= -fld->length, -fld->length), ph->field_item++);
            insert(ph, ph_->field_bit, 0, items * fld->size);
            set_bits(items, -fld->length, ph_->pack->data, ph->BIT);
            ph->items = items;
            break;
        case 6:
            ;
            int32_t len_bits = -fld->length;
            if(item < ph->_Qbbox || ph->_cbbox == 0)
            {
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                ph->BYTE = ph_->_kbbox;
                int32_t ins_items = ph->_cbbox == 0 ? 1 : ph->_Qbbox - item;
                insert(ph, ph_->field_bit, ins_items + len_bits, items * fld->size);
                ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                set_bits(ph->_Qbbox = item, fld->_Sbbox, ph_->pack->data,
                         ph_->_ebbox - fld->_wbbox + fld->_Sbbox);
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            else if(item < ph->_Qbbox + ph->_cbbox)
            {
                if(item < ph->field_item)
                {
                    ph->field_item = ph->_Qbbox;
                    ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                    ph->BYTE = ph_->_kbbox;
                }
                else
                {
                    ph->BIT--;
                    ph->field_item++;
                    ph->BYTE += ph->items * fld->size;
                }
                for(; ph->field_item < item; ph->BIT--, ph->field_item++)
                    if((ph_->pack->data[ph->BIT >> 3] & 1 << (ph->BIT & 7)) != 0)
                    {
                        ph->BIT -= len_bits;
                        ph->items = (int)get_bits(ph_->pack->data, ph->BIT, len_bits);
                        ph->BYTE += ph->items * fld->size;
                    }
                insert(ph, ph_->field_bit, len_bits, items * fld->size);
            }
            else
            {
                int32_t ins_items = item - (ph->_Qbbox + ph->_cbbox) + 1;
                ph->BIT = ph_->_Pbbox;
                ph->BYTE = ph_->_Xbbox;
                insert(ph, ph_->field_bit, ins_items + len_bits, items * fld->size);
                ph->BIT = ph_->_Pbbox + len_bits;
                ph->BYTE = ph_->_Xbbox - items * fld->size;
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            ph_->pack->data[ph->BIT >> 3] |= 1 << (ph->BIT & 7);
            set_bits(ph->items = items, len_bits, ph_->pack->data, ph->BIT -= len_bits);
            ph->items = items;
            break;
        case 8:
        case 10:
            ;
            int32_t bits = ph->items * fld->size;
            if(item < ph->_Qbbox || ph->_cbbox == 0)
            {
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                ph->BYTE = ph_->_kbbox;
                int32_t ins_items = ph->_cbbox == 0 ? 1 : ph->_Qbbox - item;
                insert(ph, ph_->field_bit, ins_items + bits, 0);
                ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                set_bits(ph->_Qbbox = item, fld->_Sbbox, ph_->pack->data,
                         ph_->_ebbox - fld->_wbbox + fld->_Sbbox);
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            else if(item < ph->_Qbbox + ph->_cbbox)
            {
                if(item < ph->field_item)
                {
                    ph->field_item = ph->_Qbbox;
                    ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                }
                else
                {
                    ph->BIT--;
                    ph->field_item++;
                }
                for(; ph->field_item < item; ph->BIT--, ph->field_item++)
                    if((ph_->pack->data[ph->BIT >> 3] & 1 << (ph->BIT & 7)) != 0) ph->BIT -= bits;
                insert(ph, ph_->field_bit, bits, 0);
            }
            else
            {
                int32_t ins_items = item + 1 - (ph->_Qbbox + ph->_cbbox);
                ph->BIT = ph_->_Pbbox;
                insert(ph, ph_->field_bit, ins_items + bits, 0);
                ph->BIT = ph_->_Pbbox + bits;
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            ph_->pack->data[ph->BIT >> 3] |= 1 << (ph->BIT & 7);
            ph->BIT -= bits;
            break;
        case 11:
            if(item < ph->field_item)
            {
                ph->field_item = -1;
                ph->BIT = ph_->_ebbox - fld->_wbbox;
            }
            if(ph->field_item < item)
                do
                {
                    int32_t bits = (int)(fld->size * get_bits(ph_->pack->data, ph->BIT -= -fld->length, -fld->length));
                    ph->BIT -= bits;
                    ph->field_item++;
                }
                while(ph->field_item < item);
            insert(ph, ph_->field_bit, items * fld->size, 0);
            set_bits(items, -fld->length, ph_->pack->data, ph->BIT);
            ph->BIT -= items * fld->size;
            ph->items = items;
            break;
        case 12:
            len_bits = -fld->length;
            if(item < ph->_Qbbox || ph->_cbbox == 0)
            {
                ph->BIT = ph_->_ebbox - fld->_wbbox;
                int32_t ins_items = ph->_cbbox == 0 ? 1 : ph->_Qbbox - item;
                insert(ph, ph_->field_bit, ins_items + len_bits + items * fld->size, 0);
                ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                set_bits(ph->_Qbbox = item, fld->_Sbbox, ph_->pack->data,
                         ph_->_ebbox - fld->_wbbox + fld->_Sbbox);
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            else if(item < ph->_Qbbox + ph->_cbbox)
            {
                if(item < ph->field_item)
                {
                    ph->field_item = ph->_Qbbox;
                    ph->BIT = ph_->_ebbox - fld->_wbbox - 1;
                }
                else
                {
                    ph->BIT--;
                    ph->field_item++;
                }
                for(; ph->field_item < item; ph->BIT--, ph->field_item++)
                    if((ph_->pack->data[ph->BIT >> 3] & 1 << (ph->BIT & 7)) != 0)
                    {
                        ph->BIT -= len_bits;
                        ph->items = (int)get_bits(ph_->pack->data, ph->BIT, len_bits);
                        ph->BIT -= ph->items * fld->size;
                    }
                insert(ph, ph_->field_bit, len_bits + items * fld->size, 0);
            }
            else
            {
                int32_t ins_items = item - (ph->_Qbbox + ph->_cbbox) + 1;
                ph->BIT = ph_->_Pbbox;
                insert(ph, ph_->field_bit, ins_items + len_bits + items * fld->size, 0);
                ph->BIT = ph_->_Pbbox + items * fld->size + len_bits;
                set_bits(ph->_cbbox += ins_items, fld->_Sbbox, ph_->pack->data, ph_->_ebbox - fld->_wbbox);
            }
            ph_->pack->data[ph->BIT >> 3] |= 1 << (ph->BIT & 7);
            set_bits(ph->items = items, len_bits, ph_->pack->data, ph->BIT -= len_bits);
            ph->BIT -= items * fld->size;
            ph->items = items;
            break;
    }
    ph->field_item = item;
}


